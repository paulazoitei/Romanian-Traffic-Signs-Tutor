              provoace desprinderea remorcii sau dezechilibrarea ansamblului;<br />
              l) autovehiculul, tractorul agricol sau forestier ori tramvaiul
              are aplicate pe parbriz, lunetă sau geamurile laterale afişe sau
              reclame publicitare, folii neomologate şi/sau nemarcate
              corespunzător ori accesorii care restrâng sau estompează
              vizibilitatea în timpul mersului, atât din interior, cât şi din
              exterior;<br />
              m) autovehiculul şi tractorul agricol sau forestier are aplicat pe
              partea frontală şi/sau posterioară a acestuia afişe, înscrisuri
              sau reclame care diminuează eficacitatea dispozitivelor de
              iluminare şi semnalizare luminoasă ori citirea numărului de
              înmatriculare;<br />
              n) autovehiculul şi tractorul agricol sau forestier prezintă
              scurgeri semnificative de carburant sau lubrifiant;<br />
              o) plăcuţele cu numărul de înmatriculare sau de înregistrare nu
              sunt conforme cu standardul ori au aplicate dispozitive de
              iluminare, altele decât cele omologate;<br />
              p) datele din certificatul de înmatriculare sau de înregistrare nu
              concordă cu caracteristicile tehnice ale vehiculului;<br />
              r) vehiculul nu a fost radiat din circulaţie în cazurile prevăzute
              în prezenta ordonanţă de urgenţă;<br />
              s) vehiculul nu este asigurat de răspundere civilă în caz de
