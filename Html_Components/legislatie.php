              art. 334 alin. (1) şi (3) din Codul penal, dovada înlocuitoare a
              permisului de conducere se eliberează cu drept de circulaţie
              pentru o perioadă de 15 zile.<br />
              <b>(6)</b> La cererea titularului permisului de conducere reţinut
