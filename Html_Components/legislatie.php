              Justiției, precum și autovehiculele de serviciu ale procurorilor
              din Ministerul Public, atunci când se deplasează în acțiuni de
              intervenție sau în misiuni care au caracter de urgență;<br />
              c) pentru lumina galbenă - autovehiculele cu mase şi/sau
              dimensiuni de gabarit depăşite ori care însoţesc asemenea
