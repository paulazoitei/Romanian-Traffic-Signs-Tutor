              <b>Art. 11.1</b><br />
              <b>(1)</b> Autorităţile competente să realizeze operaţiuni de
              înmatriculare, autorizare provizorie şi eliberare a permiselor de
              conducere încasează:<br />
              a) tarifele aferente confecţionării şi valorificării plăcilor cu
              numere de înmatriculare permanentă, de probă, temporare şi
              provizorii, atribuirii unui număr de înmatriculare preferenţial
              sau păstrării combinaţiei numărului de înmatriculare;<br />
              b) tarifele aferente emiterii certificatelor de înmatriculare, a
              autorizaţiilor provizorii şi a permiselor de conducere.<br />
              <b>(2)</b> Tarifele menţionate la alin. (1) lit. a) pot fi
              achitate prin virament, prin mijloace de plată online, prin
              intermediul POS-urilor instalate la unităţile Trezoreriei Statului
              sau la sediile instituţiilor publice beneficiare ale sumelor şi
              prin alte modalităţi de plată reglementate de acte normative în
              vigoare, prin mandat poştal sau în numerar la ghişeele serviciilor
              publice comunitare regim permise de conducere şi înmatriculare a
              vehiculelor din cadrul instituţiilor prefectului, la casieriile
              unităţilor Trezoreriei Statului, în condiţiile legii. Pentru
              sumele încasate prin intermediul POS-urilor instalate la unităţile
              Trezoreriei Statului, comisioanele se suportă de la bugetul de
              stat, prin bugetul instituţiilor publice din structura cărora fac
              parte unităţile Trezoreriei Statului, iar pentru cele încasate
              prin intermediul POS-urilor instalate la sediile instituţiilor
              publice beneficiare, comisioanele se suportă din bugetele
              acestora.<br />
              <b>(3)</b> Tarifele menţionate la alin. (1) lit. b) se încasează
              într-un cont distinct de disponibil deschis pe numele Regiei
              Autonome «Administraţia Patrimoniului Protocolului de Stat», prin
              următoarele mijloace de plată: prin virament, prin mijloace de
              plată online, inclusiv prin Sistemul naţional electronic de plată
              online - SNEP, prin intermediul POS-urilor instalate de către
              Regia Autonomă «Administraţia Patrimoniului Protocolului de Stat»
              la sediile serviciilor publice emitente ale documentelor, prin
              mandat poştal, precum şi prin alte modalităţi de plată
              reglementate de acte normative în vigoare.<br />
              <b>(4)</b> Tarifele prevăzute la alin. (1) lit. a) se încasează
              într-un cont distinct de disponibil deschis la unităţile
              teritoriale ale Trezoreriei Statului pe numele instituţiei
              prefectului din care face parte serviciul public comunitar regim
              permise de conducere şi înmatriculare a vehiculelor.<br />
              <b>(5)</b> Unităţile teritoriale ale Trezoreriei Statului la care
              îşi au deschise conturile instituţiile prefectului din care fac
              parte serviciile publice comunitare regim permise conducere şi
              înmatriculare a vehiculelor încasează tarifele prevăzute la alin.
              (1) lit. a) în numerar, în contul prevăzut la alin. (4).<br />
              <b>(6)</b> Sumele încasate în numerar la ghişeele serviciilor
              publice comunitare regim permise de conducere şi înmatriculare a
              vehiculelor se depun la casieriile instituţiilor prefectului în
              aceeaşi zi, la terminarea programului de lucru.<br />
              <b>(7)</b> Sumele predate la instituţiile prefectului potrivit
              alin. (6) se depun de către acestea în contul distinct de
              disponibil prevăzut la alin. (4) în următoarea zi lucrătoare.<br />
              <b>(8)</b> Tarifele prevăzute la alin. (1) lit. a), încasate în
              contul prevăzut la alin. (4), se virează de către instituţiile
              prefectului, în termen de cel mult două zile lucrătoare de la
              încasare, în contul de venituri al bugetului activităţii finanţate
              integral din venituri proprii «Venituri din prestări servicii» al
              Direcţiei Regim Permise de Conducere şi Înmatriculare a
              Vehiculelor.<br />
              <b>(9)</b> Restituirea sumelor prevăzute la alin. (1), achitate
              eronat sau pentru care nu au fost prestate serviciile aferente, se
              face de către Direcţia Regim Permise de Conducere şi Înmatriculare
              a Vehiculelor, respectiv de către Regia Autonomă "Administraţia
              Patrimoniului Protocolului de Stat", la cererea scrisă a
              plătitorului adresată acestora, însoţită de documentul prin care
              s-a efectuat plata. Cererea şi documentul prin care s-a efectuat
              plata pot fi transmise atât în format fizic, cât şi prin mijloace
              electronice.<br /><br />
              <b>Art. 12</b><br />
              <b>(1)</b> Vehiculele care circulă pe drumurile publice trebuie să
              fie înmatriculate sau înregistrate, după caz, cu excepţia
              următoarelor categorii de vehicule:<br />
              a) vehiculele trase sau împinse cu mâna;<br />
              b) bicicletele;<br />
              c) trotinetele electrice.<br />
              <b>(2)</b> Pentru a circula pe drumurile publice, vehiculele
              înmatriculate ori înregistrate, după caz, trebuie să poarte
              plăcuţe cu numărul de înmatriculare sau de înregistrare, cu forme,
              dimensiuni şi conţinut prevăzute de standardele în vigoare.<br />
              <b>(3)</b> Vehiculele care nu sunt supuse înmatriculării sau
              înregistrării pot circula pe drumurile publice numai în condiţiile
              prevăzute prin regulament.<br />
              <b>(4)</b> Este interzisă conducerea pe drumurile publice a altor
              vehicule decât cele care trebuie înmatriculate sau înregistrate,
              vehiculele trase sau împinse cu mâna, bicicletele şi trotinetele
              electrice.<br /><br />
              <b>Art. 13</b><br />
              <b>(1)</b> Autovehiculele şi remorcile se înmatriculează permanent
              sau temporar la autoritatea competentă în a cărei rază teritorială
              proprietarii îşi au domiciliul, reşedinţa ori sediul, în
              condiţiile stabilite prin reglementările în vigoare.<br />
              <b>(2)</b> Sunt exceptate de la prevederile alin. (1) mopedele,
              troleibuzele, remorcile destinate a fi tractate de tractoarele
              agricole sau forestiere şi vehiculele lente.<br />
              <b>(3)</b> Autovehiculele, remorcile și tractoarele agricole sau
              forestiere din dotarea Ministerului Apărării Naționale, a
              Ministerului Afacerilor Interne, a Serviciului de Protecție și
              Pază, precum și cele ale Serviciului Român de Informații se
              înregistrează la aceste instituții.<br />
              <b>(4)</b> Autovehiculele și remorcile destinate a fi tractate de
              acestea, menționate la alin. (3), pot, după caz, să fie
              înmatriculate.<br />
              <b>(5)</b> Până la înmatriculare, vehiculele prevăzute la alin.
              (1) pot circula cu numere provizorii, pe baza unei autorizaţii
              speciale eliberate de autoritatea competentă.<br />
              <b>(6)</b> La cerere, instituțiilor din sistemul de apărare,
              ordine publică și securitate națională, precum și persoanelor
              juridice abilitate să fabrice, să asambleze, să caroseze ori să
              efectueze activități de testare a vehiculelor, sistemelor,
              componentelor, unităților tehnice separate, pieselor sau
              echipamentelor destinate acestora li se pot elibera, pentru aceste
              vehicule, autorizații și numere pentru probe.<br />
              <b>(7)</b> Pot beneficia de autorizații și numere pentru probe
              vehiculele care se supun înmatriculării și se comercializează, în
              vederea efectuării probelor solicitate de clienți, la cererea
              persoanelor juridice care au ca obiect de activitate
              comercializarea vehiculelor rutiere.<br />
              <b>(8)</b> Autorizația de circulație pentru probe este valabilă
              doar pe teritoriul României, pentru cazurile prevăzute la alin.
              (6), și doar pe teritoriul județului sau al municipiului București
              în raza căruia societatea își are sediul, pentru cazul prevăzut la
              alin. (7).<br />
              <b>(9)</b> Evidența vehiculelor înmatriculate se ține la
              autoritatea competentă pe raza căreia proprietarul sau deținătorul
              mandatat își are domiciliul, reședința sau sediul.<br /><br />
              <b>Art. 14</b><br />
              <b>(1)</b> Tramvaiele, troleibuzele, mopedele, tractoarele
              agricole sau forestiere, altele decât celeprevăzute la art. 13
              alin. (3), inclusiv remorcile destinate a fi tractate de acestea,
              vehiculele lente, precum și vehiculele cu tracțiune animală se
              înregistrează la nivelul primarilor comunelor, ai orașelor, ai
              municipiilor și ai sectoarelor municipiului București.<br />
              <b>(2)</b> Evidența vehiculelor înregistrate se ține la
              autoritatea competentă prevăzută la alin. (1) pe raza căreia
              proprietarul sau deținătorul mandatat își are domiciliul,
              reședința sau sediul, care înscrie datele de identificare a
              proprietarului și a vehiculului prevăzute la art. 11 alin.
              (11)-(13) în Registrul de evidență a vehiculelor înregistrate,
              constituit la nivelul Direcției Generale Permise de Conducere și
              Înmatriculări. Înscrierea datelor se realizează prin intermediul
              aplicațiilor informatice puse la dispoziția autorităților
              administrației publice locale competente, în mod gratuit, de către
              Direcția Generală Permise de Conducere și Înmatriculări.<br />
              <b>(3)</b> Prelucrarea unor date din Registrul de evidență a
              vehiculelor înregistrate, inclusiv a unor date cu caracter
              personal, de către autoritățile și instituțiile publice cu
              atribuții în domeniul apărării, ordinii publice și securității
              naționale se realizează pe bază de protocol încheiat între acestea
              și Direcția Generală Permise de Conducere și Înmatriculări.<br /><br />
