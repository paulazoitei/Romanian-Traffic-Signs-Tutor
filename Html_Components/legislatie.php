              <b>Art. 29</b><br />
              <b>(1)</b> Circulaţia pe drumurile publice se desfăşoară în
              conformitate cu regulile de circulaţie şi cu respectarea
              semnificaţiei semnalizării rutiere realizate prin mijloacele de
              semnalizare, semnalele şi indicaţiile poliţistului rutier care
              dirijează circulaţia, semnalele speciale de avertizare luminoase
              sau sonore, de semnalizare temporară şi semnalele conducătorilor
              de vehicule.<br />
              <b>(2)</b> Participanţii la trafic sunt obligaţi să respecte şi
              semnalele poliţiştilor de frontieră, ale îndrumătorilor de
              circulaţie ai Ministerului Apărării Naţionale, ale personalului
              echipelor mobile din cadrul Autorităţii Vamale Române, precum şi
              ale organelor fiscale din cadrul Agenţiei Naţionale de
              Administrare Fiscală, aflate în îndeplinirea atribuţiilor de
              serviciu, ale agenţilor căilor ferate, ale persoanelor desemnate
              pentru dirijarea circulaţiei pe sectoarele de drum pe care se
              execută lucrări de reabilitare a acestora, ale membrilor
              patrulelor şcolare de circulaţie care acţionează în imediata
              apropiere a unităţilor de învăţământ, precum şi ale nevăzătorilor,
              potrivit prevederilor din regulament.<br /><br />
              <b>Art. 30</b><br />
              <b>(1)</b> Mijloacele de semnalizare rutieră sunt:<br />
              a) sistemele de semnalizare luminoasă sau sonoră;<br />
              b) indicatoarele;<br />
              c) marcajele;<br />
              d) alte dispozitive speciale.<br />
              <b>(2)</b> Mijloacele de semnalizare rutieră se constituie într-un
              sistem unitar, se realizează şi se instalează astfel încât să fie
              observate cu uşurinţă şi de la o distanţă adecvată, atât pe timp
              de zi, cât şi pe timp de noapte, de cei cărora li se adresează şi
              trebuie să fie în deplină concordanţă între ele, precum şi într-o
              stare tehnică de funcţionare corespunzătoare.<br />
              <b>(3)</b> Semnalele luminoase pentru dirijarea circulaţiei în
              intersecţii au următoarele semnificaţii:<br />
              a) semnalul de culoare verde permite trecerea;<br />
              b) semnalul de culoare roşie interzice trecerea;<br />
              c) semnalul de culoare galbenă împreună cu cel de culoare roşie
              interzic trecerea.<br />
              <b>(4)</b> Mijloacele de semnalizare rutieră, precum şi alte
              dispozitive speciale de acest fel se asigură, se instalează şi se
              întreţin prin grija administratorului drumului public respectiv
              ori al căii ferate, după caz. Instalarea mijloacelor de
              semnalizare rutieră şi a dispozitivelor speciale se execută numai
              cu avizul prealabil al poliţiei rutiere.<br />
              <b>(5)</b> Mijloacele de semnalizare şi presemnalizare a
              intersecţiilor dintre două drumuri de categorii diferite se
              asigură, se instalează şi se întreţin de către administratorul
              fiecărui drum, cu respectarea modului de reglementare a
              circulaţiei stabilit în nodul rutier respectiv.<br />
              <b>(6)</b> Mijloacele de semnalizare rutieră pot fi însoţite şi de
              dispozitive speciale de avertizare.<br />
              <b>(7)</b> Se interzic:<br />
              a) amplasarea, în zona drumului public, de construcţii, panouri
              sau dispozitive ce pot fi confundate cu indicatoarele sau cu
              instalaţiile ce servesc la semnalizarea rutieră ori realizarea de
              amenajări sau alte obstacole care sunt de natură să limiteze
              vizibilitatea ori eficacitatea acestora, să stânjenească
              participanţii la trafic sau să le distragă atenţia, punând în
              pericol siguranţa circulaţiei;<br />
              b) lipirea de afişe, inscripţii sau înscrisuri pe indicatoarele
              ori dispozitivele ce servesc la semnalizarea rutieră, inclusiv pe
              suporturile acestora.<br /><br />
              <b>Art. 31</b>
              Participanţii la trafic trebuie să respecte regulile de
              circulaţie, semnalele, indicaţiile şi dispoziţiile poliţistului
              rutier, precum şi semnificaţia diferitelor tipuri de mijloace de
              semnalizare rutieră, în următoarea ordine de prioritate:<br />
              a) semnalele, indicaţiile şi dispoziţiile poliţistului rutier;<br />
              b) semnalele speciale de avertizare, luminoase sau sonore ale
              autovehiculelor, prevăzute la art. 32 alin. (2) lit. a) şi b);<br />
              c) semnalizarea temporară și/sau indicatoarele electronice, care
              modifică regimul normal de desfășurare a circulației;<br />
              d) semnalele luminoase sau sonore;<br />
              e) indicatoarele;<br />
              f) marcajele;<br />
              g) regulile de circulaţie.<br /><br />
              <b>Art. 32</b><br />
              <b>(1)</b> Semnalele speciale de avertizare luminoase sunt emise
              intermitent de dispozitivele de iluminare montate pe autovehicule
              şi au următoarele semnificaţii:<br />
              a) lumina roşie obligă participanţii la trafic să oprească în
              direcţia de mers cât mai aproape de marginea drumului;<br />
              b) lumina albastră obligă participanţii la trafic să acorde
              prioritate de trecere;<br />
              c) lumina galbenă obligă participanţii la trafic să circule cu
              atenţie.<br />
              <b>(2)</b> Sunt autorizate să utilizeze semnale speciale de
              avertizare luminoase:<br />
              a) pentru lumina roşie - autovehiculele aparţinând poliţiei şi
