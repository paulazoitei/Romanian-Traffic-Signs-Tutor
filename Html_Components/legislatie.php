              <b>Art. 15</b><br />
              <b>(1)</b> Odată cu realizarea operațiunilor de înmatriculare a
              vehiculului, autoritatea competentă eliberează proprietarului sau
              deținătorului mandatat un certificat de înmatriculare, conform
              categoriei din care face parte vehiculul respectiv, precum și
              plăcuțe cu numărul de înmatriculare. La cererea proprietarului sau
              deținătorului mandatat al vehiculului, autoritatea competentă
              eliberează, pentru situațiile în care se emite un certificat de
              înmatriculare, o dovadă înlocuitoare a acestuia care atestă
              înmatricularea vehiculului, valabilă până la emiterea
              certificatului de înmatriculare, dar nu mai mult de 30 de zile.<br />
              <b>(1.1)</b> La cererea scrisă a proprietarului unui vehicul, în
              certificatul de înmatriculare sau de înregistrare se poate înscrie
              şi o altă persoană decât proprietarul, specificându-se calitatea
              în care aceasta poate utiliza vehiculul, în virtutea unui drept
              legal. În cazul în care proprietarul vehiculului este o societate
              de leasing, este obligatorie menţionarea în certificatul de
              înmatriculare sau de înregistrare şi a datelor de identificare ale
              deţinătorului mandatat.<br />
              <b>(2)</b> Abrogat.<br />
              <b>(3)</b> Pentru vehiculele prevăzute la art. 13 alin. (3) și
              art. 14 alin. (1), autoritatea care le înregistrează eliberează
              proprietarilor acestora certificate și plăcuțe cu numere de
              înregistrare, conform categoriei din care fac parte vehiculele
              respective.<br />
              <b>(4)</b> Forma, dimensiunile şi conţinutul certificatului de
              înmatriculare şi ale certificatului de înregistrare se stabilesc
              prin ordin al ministrului administraţiei şi internelor, care se
              publică în Monitorul Oficial al României, Partea I.<br />
              <b>(5)</b> Este interzisă punerea în circulație a unui vehicul,
              înmatriculat sau înregistrat, care nu are montate plăcuțe cu
              numărul de înmatriculare sau de înregistrare atribuite de
              autoritatea competentă ori dacă acestea nu sunt conforme cu
              standardele în vigoare, precum și în cazul în care certificatul de
              înmatriculare sau de înregistrare este reținut, iar dovada
              înlocuitoare a acestuia este eliberată fără drept de circulație
              sau termenul de valabilitate a expirat. Este asimilată
              interdicției de a pune în circulație un vehicul și situația în
              care conducătorul acestuia nu a prezentat documentul de
              înmatriculare ori de înregistrare la momentul constatării faptei
              pentru care prezenta ordonanță de urgență prevede reținerea
              certificatului de înmatriculare sau de înregistrare.<br /><br />
              <b>Art. 16</b> Înmatricularea, înregistrarea sau atribuirea
              numărului provizoriu ori pentru probe a unui vehicul se anulează
              de către autoritatea care a efectuat-o, dacă se constată că au
              fost încălcate normele legale referitoare la aceste operaţiuni.<br /><br />
              <b>Art. 17</b><br />
              <b>(1)</b> Radierea din evidență a vehiculelor se face de către
              autoritatea care a efectuat înmatricularea sau înregistrarea, la
              cererea proprietarului, numai în următoarele cazuri:<br />
              a) proprietarul dorește retragerea din circulație a vehiculului și
              face dovada depozitării acestuia într-un spațiu adecvat, deținut
              în condițiile legii;<br />
              b) proprietarul face dovada dezmembrării, casării sau predării
              vehiculului la unități specializate în vederea dezmembrării,
              situație în care radierea este definitivă;<br />
              c) la scoaterea din România a unui vehicul, în vederea
              înmatriculării în alt stat;<br />
              d) în cazul furtului vehiculului.<br />
              <b>(2)</b> Radierea din evidenţă a vehiculelor înregistrate, la
              trecerea acestora în proprietatea altei persoane, se face de către
              autoritatea care a efectuat înregistrarea, la cererea
              proprietarului, în condiţiile legii.<br />
              <b>(3)</b> Este interzisă circulaţia pe drumurile publice a
              vehiculelor radiate din evidenţă.<br />
              <b>(4)</b> Vehiculele declarate, potrivit legii, prin dispoziţie a
              autorităţii administraţiei publice locale, fără stăpân sau
              abandonate se radiază din oficiu în termen de 30 de zile de la
              primirea dispoziţiei respective.<br />
              <b>(5)</b> Radierea din evidenţă a unui vehicul se face, din
              oficiu, de către autoritatea care a efectuat înmatricularea, şi în
              următoarele situaţii:<br />
              a) în cazul notificării primite de la autorități competente sau
              operatori economici autorizați să emită certificatul de
              distrugere, care atestă faptul că vehiculul a fost definitiv scos
              din uz;<br />
              b) în cazul notificării primite de la autorități competente, care
              atestă faptul că vehiculul a fost înmatriculat permanent în alt
              stat;<br />
              c) la notificarea Ministerului Afacerilor Externe, în cazul
              autovehiculelor și remorcilor aparținând misiunilor diplomatice,
              oficiilor consulare și membrilor acestora, precum și altor
              organizații și persoane străine cu statut diplomatic sau
              consular;<br />
              d) d) în baza unei hotărâri judecătorești rămase definitive.<br />
              <b>(6)</b> În termen de 30 de zile de la data radierii vehiculului
              în condiţiile prevăzute la alin. (5), autoritatea competentă îl
              informează pe titularul certificatului de înmatriculare cu privire
              la efectuarea acestei operaţiuni.<br /><br />
              <b>Art. 18</b> În cazul pierderii, furtului, schimbării numelui
              ori deteriorării certificatului de înmatriculare sau de
              înregistrare, proprietarul vehiculului respectiv este obligat să
              solicite autorităţii competente eliberarea unui nou certificat de
              înmatriculare sau de înregistrare, în condiţiile stabilite de
              autoritatea competentă, în termen de 30 de zile de la data
              declarării pierderii sau furtului, de la data schimbării numelui
              ori de la data constatării deteriorării, după caz.<br /><br />
              <b>Art. 19</b> Procedura înmatriculării, înregistrării, radierii
              şi eliberarea autorizaţiei de circulaţie provizorie sau pentru
              probe a vehiculelor se stabilesc prin ordin al ministrului
              administraţiei şi internelor, care se publică în Monitorul Oficial
              al României, Partea I.
