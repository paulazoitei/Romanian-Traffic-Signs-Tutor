            <p class="question">Capitolul II: Vehiculele</p>
            <p class="answer">
              <b
                >SECŢIUNEA 1<br />
                Condiţiile privind circulaţia vehiculelor şi controlul
                acestora</b
              ><br /><br />
              <b>Art. 7.</b> Orice vehicul care circulă pe drumurile publice
              trebuie să corespundă normelor tehnice privind siguranţa
              circulaţiei rutiere, protecţia mediului şi utilizarea conform
              destinaţiei.<br /><br />
              <b>Art. 8.</b> Pentru a fi conduse pe drumurile publice, fiecare
              autovehicul, tractor agricol sau forestier şi tramvai trebuie să
              fie dotat cu trusă medicală de prim ajutor, două triunghiuri
              reflectorizante şi un stingător de incendiu, omologate.<br /><br />
              <b>Art. 9.</b><br />
              <b>(1)</b> Pentru a fi înmatriculate, înregistrate sau admise în
              circulaţie, autovehiculele, remorcile şi tramvaiele trebuie să fie
              omologate în condiţiile legii.<br />
              <b>(1.1)</b> Sunt exceptate de la prevederile alin. (1) vehiculele
              lente şi maşinile autoropulsate.<br />
              <b>(1.2)</b> Omologarea este opțională în cazul vehiculelor
              prevăzute la art. 13 alin. (3) care îndeplinesc condițiile
              prevăzute la art. 2 alin. (2) lit. d) și alin. (3) lit. b) din
              Regulamentul (UE) 2018/858 al Parlamentului European și al
              Consiliului din 30 mai 2018 privind omologarea și supravegherea
              pieței autovehiculelor și remorcilor acestora, precum și ale
              sistemelor, componentelor și unităților tehnice separate destinate
              vehiculelor respective, de modificare a Regulamentelor (CE) nr.
              715/2007 și (CE) nr. 595/2009 și de abrogare a Directivei
              2007/46/CE, precum și în cazul celor care îndeplinesc condițiile
              prevăzute la art. 2 alin. (2) lit. e) din Regulamentul (UE) nr.
              168/2013 al Parlamentului European și al Consiliului din 15
              ianuarie 2013 privind omologarea și supravegherea pieței pentru
              vehiculele cu două sau trei roți și pentru cvadricicluri.<br />
              <b>(2)</b> Categoriile de vehicule care pot fi admise în
              circulaţie fără a fi omologate se stabilesc prin ordin al
              ministrului transporturilor, construcţiilor şi turismului, cu
              avizul Ministerului Administraţiei şi Internelor - Inspectoratul
              General al Poliţiei Române, care se publică în Monitorul Oficial
              al României, Partea I.<br />
              <b>(3)</b> Documentul care atestă omologarea este cartea de
              identitate a vehiculului, eliberată în condiţiile legii.<br />
              <b>(4)</b> Pentru a fi menținute în circulație, vehiculele
              înmatriculate sau înregistrate, cu excepția mașinilor
              autopropulsate cu o viteză maximă constructivă care nu depășește
              25 km/h, a autovehiculelor cu șenile, a tramvaielor și a
              vehiculelor cu tracțiune animală, se supun inspecției tehnice
              periodice, conform legislației în vigoare.<br />
              <b>(5)</b> Inspecţia tehnică periodică se efectuează în staţii
              autorizate sau în reprezentanţe R.A.R., conform legislaţiei în
              vigoare.<br />
              <b>(6)</b> Pentru autovehiculele şi tractoarele agricole sau
              forestiere aparţinând instituţiilor din sistemul de apărare,
              ordine publică şi siguranţă naţională, inspecţia tehnică periodică
              se poate efectua şi în staţii proprii, autorizate potrivit
              legii.<br /><br />
              <b>Art. 10.</b><br />
              <b>(1)</b> Este interzisă circulaţia pe drumurile publice a
              vehiculelor care nu corespund din punct de vedere tehnic, a celor
              neasigurate obligatoriu de răspundere civilă auto pentru
              prejudicii produse terţilor prin accidente de circulaţie, a celor
              pentru care a intervenit suspendarea înmatriculării, precum şi a
              vehiculelor înregistrate al căror termen de valabilitate a
              inspecţiei tehnice periodice a expirat ori este anulată.<br />
              <b>(2)</b> Constatarea deficienţelor vehiculelor se face de către
              poliţia rutieră, iar verificarea stării tehnice a vehiculelor
              aflate în trafic pe drumurile publice se face de către poliţia
              rutieră, împreună cu instituţiile abilitate de lege.<br /><br />
              <b>SECŢIUNEA a 2-a</b>
              <b>Înmatricularea, înregistrarea şi radierea vehiculelor</b
              ><br /><br />
              <b>Art. 11</b><br />
              <b>(1)</b> Proprietarii de vehicule sau deţinătorii mandataţi ai
              acestora sunt obligaţi să le înmatriculeze sau să le înregistreze,
              după caz, înainte de a le pune în circulaţie, conform prevederilor
              legale.<br />
              <b>(2)</b> Înmatricularea vehiculelor este continuă, de la
              admiterea în circulaţie până la scoaterea definitivă din
              circulaţie a unui vehicul din categoria celor supuse acestei
              condiţii, potrivit prevederilor prezentei ordonanţe de urgenţă, şi
              presupune următoarele operaţiuni:<br />
              a) înscrierea în evidenţele autorităţilor competente, potrivit
              legii, a dobândirii dreptului de proprietate asupra unui vehicul
              de către primul proprietar;<br />
              b) transcrierea în evidenţele autorităţilor competente, potrivit
              legii, a tuturor transmiterilor ulterioare ale dreptului de
              proprietate asupra unui vehicul.<br />
              <b>(3)</b> Operaţiunile prevăzute la alin. (2) se realizează pe
              baza datelor de identificare ale vehiculului şi ale proprietarului
              şi condiţionează eliberarea de către autorităţile competente,
              potrivit legii, a unui certificat de înmatriculare, precum şi a
              plăcuţelor cu numărul de înmatriculare atribuit şi transcrierile
              necesare în certificatul de înmatriculare şi în cartea de
              identitate a vehiculului.<br />
              <b>(4)</b> În cazul transmiterii dreptului de proprietate asupra
              unui vehicul, datele noului proprietar se înscriu în Registrul
              naţional de evidenţă a permiselor de conducere şi a vehiculelor
              înmatriculate constituit de Direcţia Regim Permise de Conducere şi
              Înmatriculare a Vehiculelor simultan cu menţionarea încetării
              calităţii de titular al înmatriculării a fostului proprietar.
              Pentru realizarea acestei operaţiuni, noul proprietar este obligat
              să solicite autorităţii competente de înmatriculare transcrierea
              transmiterii dreptului de proprietate, în termen de 90 de zile de
              la data dobândirii dreptului de proprietate asupra vehiculului.<br />
              <b>(4.1.)</b> Organul fiscal care operează scoaterea vehiculului
              din evidenţa fiscală a fostului proprietar are obligaţia de a
              notifica înstrăinarea acestuia în cadrul schimbului de informaţii
              prevăzut de reglementările în materie fiscală, în termen de 5 zile
              lucrătoare, autorităţii competente de înmatriculare. Aceasta face
              menţiunea privind înstrăinarea vehiculului în Registrul naţional
              de evidenţă a permiselor de conducere şi a vehiculelor
              înmatriculate constituit de Direcţia Regim Permise de Conducere şi
              Înmatriculare a Vehiculelor, în termen de 30 de zile de la
              primirea notificării.<br />
              <b>(4.2.)</b> De la data înregistrării notificării prevăzute la
              alin. (41), autoritatea competentă de înmatriculare, respectiv
              Direcţia Regim Permise de Conducere şi Înmatriculare a
              Vehiculelor, transmite instituţiilor interesate datele noului
              proprietar, la solicitarea acestora.<br />
              <b>(4.3.)</b> În situaţia în care noul proprietar nu solicită
              transcrierea transmiterii dreptului de proprietate în termenul
              prevăzut la alin. (4), înmatricularea vehiculului se suspendă de
              drept până la data transcrierii transmiterii dreptului de
              proprietate.<br />
              <b>(4.4.)</b> În cazul în care până la transcriere intervin
              transmiteri succesive ale dreptului de proprietate asupra
              vehiculului, termenul prevăzut la alin. (4) curge de la data
              primei transmiteri a dreptului de proprietate.<br />
              <b>(4.5.)</b> Suspendarea de drept a înmatriculării vehiculului
              intervine şi la data expirării sau, după caz, a anulării
              inspecţiei tehnice periodice şi încetează la data la care
              vehiculul trece o nouă inspecţie tehnică periodică.<br />
              <b>(4.6.)</b> În cazurile prevăzute la alin. (43) şi (45),
              autoritatea competentă de înmatriculare, respectiv Direcţia Regim
              Permise de Conducere şi Înmatriculare a Vehiculelor, înregistrează
              suspendarea înmatriculării în Registrul naţional de evidenţă a
              permiselor de conducere şi a vehiculelor înmatriculate. Datele
              referitoare la suspendare se şterg după 5 ani de la data încetării
              acesteia.<br />
              <b>(4.7.)</b> Înregistrarea suspendării înmatriculării potrivit
              alin. (4.6.) în cazul prevăzut la alin.(4.5.) se realizează pe
              baza datelor referitoare la inspecţia tehnică periodică a
              vehiculului existente în Registrul naţional de evidenţă a
              permiselor de conducere şi a vehiculelor înmatriculate, comunicate
              Direcţiei Regim Permise de Conducere şi Înmatriculare a
              Vehiculelor de către Registrul Auto Român în condiţii stabilite
              prin protocol.<br />
              <b>(4.8.)</b> În scopul verificării efectuării inspecţiei tehnice
              periodice a vehiculului, respectiv a valabilităţii acesteia,
              poliţia rutieră are drept de acces pentru consultarea Registrului
              naţional de evidenţă a permiselor de conducere şi a vehiculelor
              înmatriculate în condiţiile stabilite prin protocolul încheiat
              între Inspectoratul General al Poliţiei Române şi Direcţia Regim
              Permise de Conducere şi Înmatriculare a Vehiculelor.<br />
              <b>(4.9.)</b> Direcţia Regim Permise de Conducere şi Înmatriculare
              a Vehiculelor realizează, gestionează şi pune la dispoziţia
              publicului o aplicaţie informatică, în mod gratuit, printr-un
              serviciu on-line, pe pagina proprie de internet, pentru a se
              verifica situaţia înmatriculării unui vehicul, precum şi motivul
              suspendării înmatriculării acestuia.<br />
              <b>(5)</b> Solicitanţii unei operaţiuni de înscriere a dreptului
              de proprietate în evidenţele autorităţilor competente pentru un
              vehicul rutier utilizat dobândit din alt stat trebuie să facă
              dovada certificării autenticităţii vehiculului de către Registrul
              Auto Român. Certificarea autenticităţii vehiculului atestă faptul
              că formularul-tip al documentului care confirmă înmatricularea
              anterioară a vehiculului, eliberat de autorităţile competente din
              ţara de provenienţă, este autentic.<br />
              <b>(6)</b> abrogat.<br />
              <b>(7)</b> Cu ocazia realizării oricărei operaţiuni privind
              înmatricularea unui vehicul, verificarea efectuării inspecţiei
              tehnice periodice şi a asigurării obligatorii de răspundere
              civilă, respectiv a valabilităţii acestora, se poate face şi pe
              cale informatică, în condiţiile stabilite prin protocol încheiat
              între autoritatea competentă din subordinea Ministerului
              Administraţiei şi Internelor cu atribuţii de organizare şi
              coordonare a activităţii de evidenţă şi eliberare a certificatelor
              de înmatriculare şi a plăcuţelor cu numere de înmatriculare şi
              Registrul Auto Român, respectiv Comisia de Supraveghere a
              Asigurărilor.<br />
              <b>(8)</b> abrogat.<br />
              <b>(9)</b> Societăţile abilitate să comercializeze vehicule noi şi
              societăţile de leasing din România pot solicita, pe cale
              informatică, înmatricularea sau autorizarea provizorie pentru
              circulaţie a vehiculelor, prin intermediul unei aplicaţii
              informatice puse la dispoziţie, în condiţiile stabilite prin
              protocol, de către autoritatea competenta din subordinea
              Ministerului Administraţiei şi Internelor cu atribuţii de
              organizare şi coordonare a activităţii de evidenţă şi eliberare a
              certificatelor de înmatriculare şi a plăcuţelor cu numere de
              înmatriculare.<br />
              <b>(10)</b> Proprietarul sau deţinătorul mandatat al unui vehicul
              este obligat să solicite autorităţii competente înscrierea în
              certificatul de înmatriculare sau de înregistrare a oricărei
              modificări a datelor de identificare a vehiculului respectiv sau,
              după caz, ale proprietarului, în termen de 30 de zile de la data
              la care a intervenit modificarea.<br />
              <b>(11)</b> Cu ocazia realizării operaţiunilor prevăzute la alin.
              (2), autorităţile competente de înmatriculare înscriu în Registrul
              naţional de evidenţă a permiselor de conducere şi a vehiculelor
              înmatriculate prevăzut la alin. (4) următoarele date în format
              electronic:<br />
              a) datele de identificare a proprietarului şi, după caz, a
              deţinătorului mandatat ori a altei persoane, în condiţiile art. 15
              alin. (1.1.);<br />
              b) datele de identificare a vehiculului.<br />
              <b>(12)</b> Datele de identificare prevăzute la alin. (11) lit. a)
              sunt:<br />
              a) pentru persoanele fizice: numele de familie, prenumele, codul
              numeric personal sau, dacă este cazul, echivalentul acestuia,
              potrivit legislaţiei naţionale aplicabile, respectiv numărul de
              identificare fiscală, locul şi data naşterii,
              domiciliul/reşedinţa, precum şi, opţional, un număr de telefon şi
              adresa de poştă electronică ale persoanei;<br />
              b) pentru persoanele juridice: denumirea, sediul, codul de
              identificare fiscală, numărul de înregistrare în registrul
              comerţului, dacă este cazul, sau codul unic de înregistrare,
              potrivit legii naţionale aplicabile, precum şi, opţional, un număr
              de telefon şi adresa de poştă electronică ale reprezentantului
              acestora.<br />
              <b>(13)</b> Datele de identificare a vehiculului care se înscriu
              în Registrul naţional de evidenţă a permiselor de conducere şi a
              vehiculelor înmatriculate şi codurile armonizate aferente acestora
              se stabilesc şi se actualizează prin ordin al ministrului
              afacerilor interne, care se publică în Monitorul Oficial al
              României, Partea I.<br />
              <b>(14)</b> Datele prevăzute la alin. (11) se şterg după 10 ani de
              la data radierii vehiculului.<br />
              <b>(15)</b> Registrul naţional de evidenţă a permiselor de
              conducere şi a vehiculelor înmatriculate se actualizează pe baza
              datelor furnizate în sistem informatizat de către Registrul Auto
              Român, referitoare la inspecţia tehnică periodică a vehiculelor.
              Transmiterea datelor se face cu titlu gratuit.<br />
              <b>(16)</b> Direcţia Regim Permise de Conducere şi Înmatriculare a
              Vehiculelor este autorizată să facă schimb de date cu autorităţile
              competente de înmatriculare din statele membre ale Uniunii
              Europene, prin obţinerea, respectiv furnizarea datelor necesare,
              în scopul verificării, înaintea înmatriculării unui vehicul, a
              statutului legal al acestuia în statul în care a fost înmatriculat
              anterior, în cazul în care există îndoieli asupra situaţiei reale
              sau juridice a vehiculului. Verificarea împreună cu autorităţile
              competente de înmatriculare din statele membre ale Uniunii
              Europene se poate realiza inclusiv prin utilizarea unei platforme
              informatice în scopul facilitării schimbului de informaţii.<br />
              <b>(17)</b> Prevederile alin. (1)-(16) se aplică în mod
              corespunzător și în cazul vehiculelor înregistrate care circulă pe
              drumurile publice, cu excepția vehiculelor cu tracțiune animală,
              în condițiile stabilite prin ordinul ministrului afacerilor
              interne prevăzut la art. 19.<br />
