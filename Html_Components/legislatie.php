            <p class="question">Capitolul V: Reguli de circulatie</p>
            <p class="answer">
              <b
                >SECŢIUNEA 1<br />
                Obligaţiile participanţilor la trafic</b
              ><br /><br />
              <b>Art. 35</b><br />
              <b>(1)</b> Participanţii la trafic trebuie să aibă un comportament
              care să nu afecteze fluenţa şi siguranţa circulaţiei, să nu pună
              în pericol viaţa sau integritatea corporală a persoanelor şi să nu
              aducă prejudicii proprietăţii publice ori private.<br />
              <b>(2)</b> Participanţii la trafic sunt obligaţi ca, la cererea
              poliţistului rutier, să înmâneze acestuia documentul de identitate
              sau, după caz, permisul de conducere, documentul de înmatriculare
              ori de înregistrare a vehiculului condus, documentele referitoare
              la bunurile transportate, precum şi alte documente prevăzute de
              lege.<br />
              <b>(3)</b> În exercitarea atribuţiilor care îi revin, poliţistul
              rutier are dreptul să verifice vehiculul, precum şi identitatea
              conducătorului sau a pasagerilor aflaţi în interiorul acestuia
              atunci când există indicii despre săvârşirea unei fapte de natură
              contravenţională sau penală.<br />
              <b>(4)</b> În exercitarea atribuţiilor de dirijare a circulaţiei
              rutiere, poliţiştii rutieri sunt obligaţi să poarte uniformă cu
              înscrisuri şi însemne distinctive.<br />
              <b>(4.1)</b> Funcţionarii din cadrul Autorităţii Vamale Române,
              respectiv al Agenţiei Naţionale de Administrare Fiscală, care, în
              scopul exercitării atribuţiilor de control specifice, efectuează
              semnale de oprire pentru conducătorii autovehiculelor, sunt
              obligaţi să poarte uniformă cu înscrisuri şi însemne distinctive
              şi mijloace de protecţie fluorescent-reflectorizante pe fond de
              culoare verde, respectiv roşie, stabilite prin hotărâre a
              Guvernului.<br />
              <b>(5)</b> Poliţiştii de frontieră, îndrumătorii de circulaţie ai
              Ministerului Apărării, agenţii căilor ferate, personalul autorizat
              din zona lucrărilor pe drumurile publice, precum şi membrii
              patrulelor şcolare de circulaţie sunt obligaţi ca, pe timpul
              exercitării atribuţiilor, să poarte echipament de
              protecţie-avertizare fluorescent-reflectorizant.<br />
              <b>(6)</b> Conducătorii autovehiculelor şi tractoarelor agricole
              sau forestiere cu masa maximă autorizată mai mare de 3,5 tone sunt
              obligaţi să poarte echipament de protecţie-avertizare
              fluorescent-reflectorizant atunci când execută intervenţii la
              vehiculul care se află pe partea carosabilă a drumului public.<br />
              <b>(7)</b> Nevăzătorii sunt obligaţi să poarte în deplasarea pe
              drumurile publice baston de culoare albă.<br /><br />
              <b>Art. 36</b><br />
              <b>(1)</b> Conducătorii de autovehicule şi tractoare agricole sau
              forestiere şi persoanele care ocupă locuri prevăzute prin
              construcţie cu centuri sau dispozitive de siguranţă omologate
              trebuie să le poarte în timpul circulaţiei pe drumurile publice,
              cu excepţia cazurilor prevăzute în regulament.<br />
              <b>(1.1)</b> Conducătorii de autovehicule având locuri prevăzute
              prin construcție cu centuri de siguranță trebuie să informeze
              pasagerii cu privire la obligația legală de a le purta în timpul
              circulației pe drumurile publice. <b>(1.2)</b> Conducătorii de
              autovehicule având locuri prevăzute prin construcție cu centuri de
              siguranță au obligația să se asigure că, pe timpul conducerii
              vehiculului, minorii poartă centurile de siguranță sau sunt
              transportați numai în dispozitive de fixare în scaune pentru copii
              omologate, în condițiile prevăzute de regulament.<br />
              <b>(1.3)</b> Conducătorilor de autovehicule le este interzis să
              transporte copii în vârstă de până la 3 ani în autovehicule care
              nu sunt echipate cu sisteme de siguranță, cu excepția celor
              destinate transportului public de persoane, precum și a taxiurilor
              dacă în acestea din urmă ocupă orice alt loc decât cel de pe
              scaunul din față, în condițiile prevăzute în regulament. Copiii cu
              vârsta de peste 3 ani, având o înălțime de până la 150 cm, pot fi
              transportați în autovehicule care nu sunt echipate cu sisteme de
              siguranță doar dacă ocupă, pe timpul transportului, orice alt loc
              decât cel de pe scaunul din față.<br />
              <b>(2)</b> Pe timpul deplasării pe drumurile publice, conducătorii
              motocicletelor, mopedelor şi persoanele transportate pe acestea au
              obligaţia să poarte cască de protecţie omologată.<br />
              <b>(3)</b> Conducătorilor de vehicule le este interzisă folosirea
              telefoanelor mobile atunci când aceştia se află în timpul
              deplasării pe drumurile publice, cu excepţia celor prevăzute cu
              dispozitive tip "mâini libere".<br /><br />
              <b>Art. 37</b><br />
              <b>(1)</b> Conducătorii de vehicule sunt obligaţi să oprească
              imediat, pe acostament sau, în lipsa acestuia, cât mai aproape de
              marginea drumului sau bordura trotuarului, în sensul de deplasare,
              la apropierea şi la trecerea autovehiculelor cu regim de
              circulaţie prioritară care au în funcţiune mijloacele speciale de
              avertizare luminoasă de culoare roşie şi sonore.<br />
              <b>(2)</b> Conducătorii de vehicule sunt obligaţi să reducă
              viteza, să circule cât mai aproape de marginea drumului în sensul
              de deplasare şi să acorde prioritate la trecerea autovehiculelor
              cu regim de circulaţie prioritară care au în funcţiune mijloacele
              speciale de avertizare luminoasă de culoare albastră şi sonore.<br />
              <b>(3)</b> În situaţiile prevăzute la alin. (1) şi (2), pietonilor
              le sunt interzise traversarea şi circulaţia pe carosabil până la
              trecerea vehiculelor respective.<br /><br />
              <b>Art. 38</b> Conducătorii vehiculelor, cu excepţia celor trase
              sau împinse cu mâna, instructorii auto atestaţi să efectueze
              instruirea practică a persoanelor pentru obţinerea permisului de
              conducere, precum şi examinatorul autorităţii competente, în
              timpul desfăşurării probelor practice ale examenului pentru
              obţinerea permisului de conducere, sunt obligaţi să se supună
              testării aerului expirat şi/sau recoltării probelor biologice în
              vederea stabilirii alcoolemiei ori a consumului de substanțe
              psihoactive, la solicitarea poliţistului rutier.<br /><br />
              <b>Art. 39</b><br />
              <b>(1)</b> La solicitarea poliției rutiere sau a poliției locale,
              proprietarul sau deținătorul mandatat al unui vehicul este obligat
              să comunice acesteia, în termenul indicat în cuprinsul
              solicitării, datele de identificare ale persoanei căreia ia
              încredințat vehiculul pentru a-l conduce pe drumurile publice.
              Poliția locală poate solicita aceste date de identificare numai în
              legătură cu exercitarea competențelor în domeniul circulației pe
              drumurile publice.<br />
              <b>(2)</b> Obligaţia de a comunica datele de identificare,
              prevăzută la alin. (1), se consideră îndeplinită dacă proprietarul
              sau deţinătorul mandatat al unui vehicul comunică numele,
              prenumele, codul numeric personal, domiciliul sau reşedinţa,
              numărul şi seria actului de identitate ale persoanei căreia i-a
              încredinţat vehiculul pentru a-l conduce pe drumurile publice ori,
              în cazul cetăţenilor străini, al persoanelor fără cetăţenie sau al
              cetăţenilor români cu domiciliul în străinătate, şi seria şi
              numărul paşaportului ori ale altui document de trecere a
              frontierei de stat, data eliberării acestuia şi statul emitent.<br />
              <b>(3)</b> Comunicarea solicitării prevăzute la alin. (1) și a
              datelor prevăzute la alin. (2) se poate realiza și prin platforme
              informatice destinate furnizării de servicii electronice, atunci
              când proprietarul sau deținătorul mandatat al unui vehicul a optat
              pentru această modalitate de comunicare.<br />
              <b>(4)</b> Solicitarea prevăzută la alin. (1) se consideră
              comunicată în termen de 5 zile de la data punerii la dispoziția
              proprietarului sau deținătorului mandatat al unui vehicul prin
              mijlocul electronic de transmitere la distanță.<br />
              <b>(5)</b> Solicitarea prevăzută la alin. (1) întocmită ca înscris
              în formă electronică, în cazul în care nu poate fi comunicată în
              modalitatea prevăzută la alin. (3), se consideră comunicată la
              data comunicării copiei acesteia, imprimată pe suport hârtie.
              Documentul astfel rezultat nu trebuie să poarte semnătura olografă
              a polițistului rutier.<br /><br />
              <b>Art. 40</b> Regulile de circulaţie pe drumurile publice
              aplicabile autovehiculelor şi tractoarelor agricole sau forestiere
              destinate transporturilor de mărfuri, celor cu mase sau cu
              dimensiuni de gabarit depăşite ori care transportă mărfuri sau
              produse periculoase se stabilesc prin regulament, în conformitate
              cu reglementările în vigoare.<br /><br />
              <b
                >SECŢIUNEA a 2-a<br />
                Reguli pentru circulaţia vehiculelor</b
              ><br /><br />
              <b
                >SUBSECŢIUNEA 1<br />
                Poziţii în timpul mersului şi circulaţia pe benzi</b
              ><br />
              <b>Art. 41</b><br />
              <b>(1)</b> Vehiculele şi animalele, atunci când circulă pe
              drumurile publice pe care le este permis accesul, trebuie conduse
              pe partea din dreapta a drumului public, în sensul de circulaţie,
              cât mai aproape de marginea părţii carosabile, cu respectarea
              semnificaţiei semnalizării rutiere şi a regulilor de
              circulaţie.<br />
              <b>(2)</b> Numerotarea benzilor de circulaţie pe fiecare sens se
              efectuează în ordine crescătoare de la marginea din partea dreaptă
              a drumului către axa acestuia. În cazul autostrăzilor, banda de
              urgenţă nu intră în numerotarea benzilor de circulaţie.<br />
              <b>(3)</b> Dacă un drum este prevăzut cu o pistă pentru biciclete,
              bicicletele şi trotinetele electrice vor fi conduse numai pe pista
              respectivă. În lipsa acestei piste, circulaţia trotinetelor
              electrice este permisă numai pe sectoarele de drum unde viteza
              maximă admisă pentru circulaţia vehiculelor este de 50 km/h.<br /><br />
              <b>Art. 42</b> Când circulaţia se desfăşoară pe două sau mai multe
              benzi pe sens, acestea se folosesc de către conducătorii de
              vehicule în funcţie de intensitatea traficului şi viteza de
              deplasare, având obligaţia să revină pe prima bandă ori de câte
              ori acest lucru este posibil, dacă aceasta nu este destinată
              vehiculelor lente sau transportului public de persoane.<br /><br />
              <b>Art. 43</b><br />
              <b>(1)</b> Dacă un drum este prevăzut cu o bandă destinată
              vehiculelor lente sau transportului public de persoane,
              semnalizată ca atare, acestea vor circula numai pe banda
              respectivă.<br />
              <b>(2)</b> Conducătorul de vehicul care circulă pe banda situată
              lângă marginea părţii carosabile trebuie să acorde prioritate de
              trecere vehiculelor care efectuează transport public de persoane
              numai atunci când conducătorii acestora semnalizează intenţia de a
              reintra în trafic din staţiile prevăzute cu alveole şi s-au
              asigurat că prin manevra lor nu pun în pericol siguranţa
              celorlalţi participanţi la trafic.<br /><br />
              <b
                >SUBSECŢIUNEA 2<br />
                Mijloacele de avertizare folosite de conducătorii de vehicule</b
              ><br />
              <b>Art. 44</b><br />
              <b>(1)</b> În circulaţia pe drumurile publice conducătorii de
              vehicule pot folosi, în condiţiile prevăzute de regulament,
              mijloacele de avertizare sonoră şi luminoasă aflate în dotare şi
              omologate.<br />
              <b>(2)</b> În circulaţia pe autostrăzi, pe drumurile expres şi pe
              cele naţionale europene (E) conducătorii de autovehicule şi
              tractoare agricole sau forestiere sunt obligaţi să folosească şi
              în timpul zilei luminile de întâlnire sau luminile pentru
              circulaţia diurnă.<br />
              <b>(3)</b> Conducătorii motocicletelor şi mopedelor sunt obligaţi
              să folosească luminile de întâlnire pe toată durata deplasării
              acestora pe drumurile publice.<br />
              <b>(4)</b> În circulaţia pe drumurile publice se interzice
              deţinerea la vedere, montarea şi folosirea mijloacelor speciale de
              avertizare sonoră şi luminoasă pe şi în alte autovehicule decât
              cele prevăzute la art. 32 alin. (2), precum şi deţinerea, montarea
              sau folosirea pe autovehicule a sistemelor care perturbă buna
              funcţionare a dispozitivelor de supraveghere a traficului.<br />
              <b>(5)</b> Conducătorii de autovehicule pot fi avertizaţi de
              poliţia rutieră în legătură cu prezenţa în trafic a dispozitivelor
              de măsurare a vitezei, prin mass-media sau panouri de avertizare.
              Conducătorii de autovehicule pot folosi mijloace proprii de
              detectare a dispozitivelor de măsurare a vitezei.<br /><br />
              <b
                >SUBSECŢIUNEA 3<br />
                Depăşirea</b
              ><br />
              <b>Art. 45</b><br />
              <b>(1)</b> Depăşirea este manevra prin care un vehicul trece
              înaintea altui vehicul ori pe lângă un obstacol, aflat pe acelaşi
              sens de circulaţie, prin schimbarea direcţiei de mers şi ieşirea
              de pe banda de circulaţie sau din şirul de vehicule în care s-a
              aflat iniţial.<br />
              <b>(2)</b> Conducătorul vehiculului care se angajează în depăşire
              trebuie să se asigure că vehiculul care circulă în faţa sau în
              spatele lui nu a iniţiat o asemenea manevră.<br />
              <b>(3)</b> Atunci când prin manevra de depăşire se trece peste axa
              care separă sensurile de circulaţie, conducătorii de vehicule
              trebuie să se asigure că din sens opus nu se apropie un vehicul şi
              că dispun de spaţiu suficient pentru a reintra pe banda iniţială,
              unde au obligaţia să revină după efectuarea manevrei de
              depăşire.<br />
              <b>(4)</b> Nu constituie depăşire, în sensul alin. (1), situaţia
              în care un vehicul circulă mai repede pe una dintre benzi decât
              vehiculele care circulă pe altă bandă în acelaşi sens de
              circulaţie.<br />
              <b>(5)</b> Depăşirea se efectuează numai pe partea stângă a
              vehiculului depăşit. Tramvaiul sau vehiculul al cărui conducător a
              semnalizat intenţia şi s-a încadrat corespunzător părăsirii
              sensului de mers spre stânga se depăşeşte prin partea dreaptă.<br />
              <b>(6)</b> Tramvaiul aflat în mers poate fi depăşit şi pe partea
              stângă atunci când drumul este cu sens unic sau când între şina
              din dreapta şi marginea trotuarului nu există spaţiu suficient.<br /><br />
              <b>Art. 46</b> Obligaţiile conducătorilor vehiculelor care
              efectuează depăşirea şi ale conducătorilor vehiculelor care sunt
              depăşite, precum şi cazurile în care depăşirea este interzisă se
              stabilesc prin regulament.<br /><br />
              <b
                >SUBSECŢIUNEA 4<br />
                Trecerea pe lângă vehiculele care circulă din sens opus</b
              ><br />
              <b>Art. 47</b> Conducătorii vehiculelor care circulă din sensuri
              opuse trebuie să păstreze între vehicule o distanţă laterală
              suficientă şi să circule cât mai aproape de marginea din dreapta a
              benzii de circulaţie respective.<br /><br />
              <b
                >SUBSECŢIUNEA 5<br />
                Viteza şi distanţa dintre vehicule</b
              ><br />
              <b>Art. 48</b> Conducătorul de vehicul trebuie să respecte regimul
              legal de viteză şi să o adapteze în funcţie de condiţiile de drum,
              astfel încât să poată efectua orice manevră în condiţii de
              siguranţă.<br /><br />
              <b>Art. 49</b><br />
              <b>(1)</b> Limita maximă de viteză în localităţi este de 50
              km/h.<br />
              <b>(2)</b> Pe anumite sectoare de drum din interiorul
              localităţilor, administratorul drumului poate stabili, pentru
              autovehiculele din categoriile A şi B, şi limite de viteză
              superioare, dar nu mai mult de 80 km/h. Limitele de viteză mai
              mari de 50 km/h se stabilesc numai cu avizul poliţiei rutiere.<br />
              <b>(3)</b> Pe anumite sectoare de drum, ţinând seama de
              împrejurări şi de intensitatea circulaţiei, administratorul
              drumului, cu avizul poliţiei rutiere, poate stabili şi limite de
              viteză inferioare, dar nu mai puţin de 10 km/h pentru tramvaie şi
              de 30 km/h pentru toate autovehiculele.<br />
              <b>(4)</b> Limitele maxime de viteză în afara localităţilor
              sunt:<br />
              a) pe autostrăzi - 130 km/h;<br />
              b) pe drumurile expres - 120 km/h;<br />
              c) pe drumurile naţionale europene (E) - 100 km/h;<br />
              d) pe celelalte categorii de drumuri - 90 km/h.<br /><br />
              <b>Art. 50</b><br />
              <b>(1)</b> Vitezele maxime admise în afara localităţilor pentru
              categoriile de autovehicule prevăzute în anexa nr. 1 sunt
              următoarele:<br />
              a) 130 km/h pe autostrăzi, 120 km/h pe drumurile expres, 100 km/h
              pe drumurile naţionale europene (E) şi 90 km/h pe celelalte
              categorii de drumuri, pentru autovehiculele din categoriile A şi
              B;<br />
              b) 110 km/h pe autostrăzi, 100 km/h pe drumurile expres, 90 km/h
              pe drumurile naţionale europene (E) şi 80 km/h pe celelalte
              categorii de drumuri, pentru autovehiculele din categoriile C, D
              şi categoria D1;<br />
              c) 90 km/h pe autostrăzi, 85 km/h pe drumuri expres, 80 km/h pe
              drumurile naţionale europene (E) şi 70 km/h pentru celelalte
              categorii de drumuri, pentru autovehiculele din categoriile A1, B1
              şi C1;<br />
              d) 45 km/h, pentru tractoarele agricole sau forestiere şi
              mopede.<br />
              <b>(2)</b> Viteza maximă admisă în afara localităţilor pentru
              autovehiculele care tractează remorci sau semiremorci este cu 10
              km/h mai mică decât viteza maximă admisă pentru categoria din care
              face parte autovehiculul trăgător.<br />
              <b>(3)</b> Viteza maximă admisă pentru autovehicule cu mase şi/sau
              gabarite depăşite ori care transportă produse periculoase este de
              40 km/h în localităţi, iar în afara localităţilor de 70 km/h.<br />
              <b>(4)</b> Viteza maximă admisă în afara localităţilor pentru
              autovehiculele ai căror conducători au mai puţin de un an practică
              de conducere sau pentru persoanele care efectuează pregătirea
              practică în vederea obţinerii permisului de conducere este cu 20
              km/h mai mică decât viteza maximă admisă pentru categoria din care
              fac parte autovehiculele conduse.<br /><br />
              <b>Art. 51</b> Conducătorul unui vehicul care circulă în spatele
              altuia are obligaţia de a păstra o distanţă suficientă faţă de
              acesta, pentru evitarea coliziunii.<br /><br />
              <b>Art. 52</b><br />
              <b>(1)</b> Este interzisă desfăşurarea de concursuri, antrenamente
              ori întreceri pe drumurile publice, cu excepţia celor autorizate
              de administratorul drumului respectiv şi avizate de poliţia
              rutieră.<br />
              <b>(2)</b> Organizatorii concursurilor, antrenamentelor ori
              întrecerilor autorizate sunt obligaţi să ia toate măsurile
              necesare pentru desfăşurarea în siguranţă a acestora, precum şi
              pentru protecţia celorlalţi participanţi la trafic.<br />
              <b>(3)</b> În caz de producere a unui eveniment rutier, ca urmare
              a neîndeplinirii atribuţiilor prevăzute la alin. (2),
              organizatorii acestora răspund administrativ, contravenţional,
              civil sau penal, după caz.<br /><br />
              <b>Art. 53</b> Autorităţile publice locale, cu autorizaţia
              administratorului drumului public şi cu avizul poliţiei rutiere
              sau la solicitarea acesteia, sunt obligate să ia măsuri pentru
              realizarea de amenajări rutiere destinate circulaţiei pietonilor,
              bicicliştilor, vehiculelor cu tracţiune animală şi calmării
              traficului, semnalizate corespunzător, în apropierea unităţilor de
              învăţământ, pieţelor, târgurilor, spitalelor, precum şi în zonele
              cu risc sporit de accidente.<br /><br />
              <b
                >SUBSECŢIUNEA 6<br />
                Reguli referitoare la manevre</b
              ><br />
              <b>Art. 54</b><br />
              <b>(1)</b> Conducătorul de vehicul care execută o manevră de
              schimbare a direcţiei de mers, de ieşire dintr-un rând de vehicule
              staţionate sau de intrare într-un asemenea rând, de trecere pe o
              altă bandă de circulaţie sau de virare spre dreapta ori spre
              stânga sau care urmează să efectueze o întoarcere ori să meargă cu
              spatele este obligat să semnalizeze din timp şi să se asigure că o
              poate face fără să perturbe circulaţia sau să pună în pericol
              siguranţa celorlalţi participanţi la trafic.<br />
              <b>(2)</b> Semnalizarea schimbării direcţiei de mers trebuie să
              fie menţinută pe întreaga durată a manevrei.<br /><br />
              <b>Art. 54.1</b><br />
              <b>(1)</b> Se interzice conducătorilor de vehicule să adopte un
              comportament agresiv în conducerea acestora pe drumurile
              publice.<br />
              <b>(2)</b> Prin comportament agresiv se înţelege efectuarea, pe
              drumul public, de către conducătorul de vehicul, a uneia dintre
              următoarele manevre:<br />
              a) deplasarea succesivă de pe o bandă de circulație pe alta sau de
              pe un rând pe altul, alternând din stânga în dreapta, în scopul
              depășirii unui șir de vehicule care circulă în același sens;<br />
              b) întoarcerea vehiculului prin folosirea frânei de ajutor;<br />
              c) pornirea vehiculului de pe loc prin patinarea excesivă, în gol,
              a roţilor motoare;<br />
              d) circulaţia cu vehiculul la o distanţă foarte redusă faţă de un
              alt vehicul, înaintea sau în spatele acestuia, cât şi în lateral,
              ori reducerea bruscă a vitezei de deplasare fără motiv întemeiat,
              de natură a intimida conducătorul acestuia;<br />
              e) folosirea repetată a semnalelor sonore şi/sau luminoase de
              natură a obliga nejustificat conducătorul de vehicul care circulă
              în faţa sa să elibereze banda de circulaţie;<br />
              f) conducerea mopedului sau motocicletei având în contact cu
              partea carosabilă numai una dintre roţi;<br />
              g) mersul cu spatele cu vehiculul în scopul intimidării celorlalţi
              participanţi la trafic care circulă în spatele acestuia;<br />
              h) realizarea intenţionată a unui derapaj controlat al vehiculului
              în vederea întoarcerii sau rotirii acestuia;<br />
              i) conducerea intenţionată a unui vehicul prin accelerarea
              repetată a motorului, de natură a stânjeni persoanele aflate în
              zona drumului public.<br /><br />
              <b
                >SUBSECŢIUNEA 7<br />
                Intersecţii şi obligaţia de a ceda trecerea</b
              ><br />
              <b>Art. 55</b><br />
              Intersecţiile sunt:<br />
              a) cu circulaţie nedirijată;<br />
              b) cu circulaţie dirijată. În această categorie sunt incluse şi
              intersecţiile în care circulaţia se desfăşoară în sens
              giratoriu.<br /><br />
              <b>Art. 56</b> La apropierea de o intersecţie conducătorul de
              vehicul trebuie să circule cu o viteză care să îi permită oprirea,
              pentru a acorda prioritate de trecere participanţilor la trafic
              care au acest drept.<br /><br />
              <b>Art. 57</b><br />
              <b>(1)</b> La intersecţiile cu circulaţie nedirijată, conducătorul
              de vehicul este obligat să cedeze trecerea vehiculelor care vin
              din partea dreaptă, în condiţiile stabilite prin regulament.<br />
              <b>(2)</b> La intersecţiile cu circulaţie dirijată, conducătorul
              de vehicul este obligat să respecte semnificaţia indicatoarelor,
              culoarea semaforului sau indicaţiile ori semnalele poliţistului
              rutier.<br />
              <b>(3)</b> Pătrunderea unui vehicul într-o intersecţie este
              interzisă dacă prin aceasta se produce blocarea intersecţiei.<br />
              <b>(4)</b> În intersecţiile cu sens giratoriu, semnalizate ca
              atare, vehiculele care circulă în interiorul acestora au
              prioritate faţă de cele care urmează să pătrundă în
              intersecţie.<br /><br />
              <b>Art. 58</b> În cazul vehiculelor care pătrund într-o
              intersecţie dintre un drum închis circulaţiei publice şi un drum
              public, au prioritate acele vehicule care circulă pe drumul
              public.<br /><br />
              <b>Art. 59</b><br />
              <b>(1)</b> În intersecţiile cu circulaţie nedirijată, conducătorul
              de vehicul este obligat să acorde prioritate de trecere
              vehiculelor care circulă pe şine. Acestea pierd prioritatea de
              trecere când efectuează virajul spre stânga sau când semnalizarea
              rutieră din acea zonă stabileşte o altă regulă de circulaţie.<br />
              <b>(2)</b> În intersecţii, conducătorii vehiculelor care virează
              spre stânga sunt obligaţi să acorde prioritate de trecere
              vehiculelor cu care se intersectează şi care circulă din partea
              dreaptă.<br />
              <b>(3)</b> În intersecţiile cu circulaţie dirijată prin
              indicatoare de prioritate, regula priorităţii de dreapta se
              respectă numai în cazul în care două vehicule urmează să se
              întâlnească, fiecare intrând în intersecţie de pe un drum
              semnalizat cu un indicator având aceeaşi semnificaţie de
              prioritate sau de pierdere a priorităţii.<br />
              <b>(4)</b> Când un semafor cu trei culori are o lumină verde
              intermitentă suplimentară, montată la acelaşi nivel cu lumina
              verde normală a semaforului, sub forma unei săgeţi verzi pe fond
              negru, cu vârful spre dreapta, aprinderea acesteia semnifică
              permisiunea pentru vehicule de a-şi continua drumul în direcţia
              indicată de săgeată, indiferent de culoarea semaforului electric,
              cu condiţia acordării priorităţii de trecere vehiculelor şi
              pietonilor care au drept de circulaţie.<br /><br />
              <b
                >SUBSECŢIUNEA 8<br />
                Trecerea la nivel cu calea ferată</b
              ><br />
              <b>Art. 60</b><br />
              <b>(1)</b> Participanţii la trafic trebuie să dea dovadă de
              prudenţă sporită la apropierea şi traversarea liniilor de cale
              ferată curentă sau industrială, după caz.<br />
              <b>(2)</b> La trecerea la nivel cu o cale ferată curentă,
              prevăzută cu bariere sau semibariere, conducătorii de vehicule
              sunt obligaţi să oprească în dreptul indicatorului ce obligă la
              oprire, dacă acestea sunt în curs de coborâre ori în poziţie
              orizontală şi/sau semnalele sonore şi luminoase care anunţă
              apropierea trenului sunt în funcţiune.<br />
              <b>(3)</b> La trecerea la nivel cu o cale ferată industrială,
              semnalizată corespunzător, conducătorii de vehicule sunt obligaţi
              să se conformeze semnificaţiei semnalelor agentului de cale
              ferată.<br /><br />
              <b
                >SUBSECŢIUNEA 9<br />
                Autovehicule cu regim de circulaţie prioritară</b
              ><br />
              <b>Art. 61</b><br />
              <b>(1)</b> Au regim de circulaţie prioritară numai autovehiculele
              prevăzute la art. 32 alin. (2) lit. a) şi b), atunci când se
              deplasează în acţiuni de intervenţie sau în misiuni care au
              caracter de urgenţă. Pentru a avea prioritate de trecere, aceste
              autovehicule trebuie să aibă în funcţiune semnalele luminoase şi
              sonore.<br />
              <b>(2)</b> Conducătorii autovehiculelor aparţinând instituţiilor
              prevăzute la art. 32 alin. (2) lit. a) şi b) pot încălca regimul
              legal de viteză sau alte reguli de circulaţie, cu excepţia celor
              care reglementează trecerea la nivel cu calea ferată, atunci când
              se deplasează în acţiuni de intervenţie sau în misiuni care au
              caracter de urgenţă.<br />
              <b>(3)</b> Când pe drumul public circulaţia este dirijată de un
              poliţist rutier, conducătorii autovehiculelor prevăzute la alin.
              (1) trebuie să respecte semnalele, indicaţiile şi dispoziţiile
              acestuia.<br />
              <b>(4)</b> În situaţia încălcării regimului legal de viteză sau a
              altor reguli de circulaţie, în condiţiile alin. (2), confirmarea
              deplasării conducătorului autovehiculului în acţiunea de
              intervenţie sau în misiunea care a avut caracter de urgenţă se
              face în scris de către instituţiile prevăzute la art. 32 alin. (2)
              lit. a) şi b), în termen de cel mult 30 de zile, la solicitarea
              poliţiei rutiere.<br /><br />
              <b>Art. 62</b><br />
              <b>(1)</b> La intrarea în intersecţiile unde lumina roşie a
              semaforului este în funcţiune ori indicatoarele obligă la
              acordarea priorităţii de trecere, conducătorii autovehiculelor
              prevăzute la art. 61 alin. (1) trebuie să reducă viteza şi să
              circule cu atenţie sporită pentru evitarea producerii unor
              accidente de circulaţie, în caz contrar urmând să răspundă
              potrivit legii.<br />
              <b>(2)</b> Când două autovehicule cu regim de circulaţie
              prioritară, care se deplasează în misiune având semnalele
              luminoase şi sonore în funcţiune, se apropie de o intersecţie,
              venind din direcţii diferite, vehiculul care circulă din partea
              dreaptă are prioritate.<br /><br />
              <b
                >SUBSECŢIUNEA 10<br />
                Oprirea, staţionarea şi parcarea</b
              ><br />
              <b>Art. 63</b><br />
              <b>(1)</b> Se consideră oprire imobilizarea voluntară a unui
              vehicul pe drumul public, pe o durată de cel mult 5 minute. Peste
              această durată, imobilizarea se consideră staţionare.<br />
              <b>(2)</b> Nu se consideră oprire:<br />
              a) imobilizarea vehiculului atât timp cât este necesară pentru
              îmbarcarea sau debarcarea unor persoane, dacă prin această manevră
              nu a fost perturbată circulaţia pe drumul public respectiv;<br />
              b) imobilizarea autovehiculului având o masă totală maximă
              autorizată de până la 3,5 tone, atât timp cât este necesar pentru
              operaţiunea de distribuire a mărfurilor alimentare la unităţile
              comerciale.<br />
              <b>(3)</b> Pentru autovehiculele care transportă mărfuri, altele
              decât cele prevăzute la alin. (2) lit. b), administratorul
              drumului public împreună cu autorităţile administraţiei publice
              locale, cu avizul poliţiei rutiere, vor stabili programe sau
              intervale orare pe timpul nopţii, în care oprirea sau staţionarea
              este permisă pentru distribuirea mărfurilor.<br />
              <b>(4)</b> Se consideră parcare staţionarea vehiculelor în spaţii
              special amenajate sau stabilite şi semnalizate corespunzător.<br />
              <b>(5)</b> Vehiculul oprit sau staţionat pe partea carosabilă
              trebuie aşezat lângă şi în paralel cu marginea acesteia, pe un
              singur rând, dacă printr-un alt mijloc de semnalizare nu se
              dispune altfel. Motocicletele fără ataş, mopedele şi bicicletele
              pot fi oprite sau staţionate şi câte două, una lângă alta.<br /><br />
              <b>Art. 64</b><br />
              <b>(1)</b> Poliţia rutieră poate dispune ridicarea vehiculelor
              staţionate neregulamentar pe partea carosabilă. Ridicarea şi
              depozitarea vehiculelor în locuri special amenajate se realizează
              de către administraţiile publice locale sau de către
              administratorul drumului public, după caz.<br />
              <b>(2)</b> Contravaloarea cheltuielilor pentru ridicarea,
              transportul şi depozitarea vehiculului staţionat neregulamentar se
              suportă de către deţinătorul acestuia.<br />
              <b>(3)</b> Ridicarea vehiculelor dispusă de poliţia rutieră în
              condiţiile prevăzute la alin. (1) se realizează potrivit
              procedurii stabilite prin regulament.<br /><br />
              <b>Art. 65</b> Cazurile şi condiţiile în care oprirea, staţionarea
              sau parcarea pe drumul public este permisă se stabilesc prin
              regulament, în conformitate cu prevederile prezentei ordonanţe de
              urgenţă.<br /><br />
              <b
                >SUBSECŢIUNEA 11<br />
                Circulaţia vehiculelor destinate transportului de mărfuri sau
                transportului public de persoane</b
              ><br />
              <b>Art. 66</b><br />
              <b>(1)</b> Atestatul profesional este obligatoriu pentru
              conducătorul autovehiculului care efectuează transport de mărfuri
              periculoase, transport public de persoane, transport în cont
              propriu de persoane cu microbuze şi autobuze, transporturi
              agabaritice, precum şi pentru autovehiculele de transport marfă cu
              masa maximă autorizată mai mare de 3,5 tone, care circulă în
              trafic intern şi internaţional.<br />
              <b>(2)</b> Prevederile alin. (1) nu se aplică conducătorilor de
              autovehicule aparținând Ministerului Afacerilor Interne,
              Ministerului Apărării Naționale, Serviciului Român de Informații,
              Serviciului de Protecție și Pază și Administrației Naționale a
              Penitenciarelor.<br />
              <b>(3)</b> Condiţiile de obţinere a certificatului de atestare
              profesională se aprobă prin ordin al ministrului transporturilor,
              construcţiilor şi turismului, în conformitate cu prevederile
              legale în vigoare.<br />
              <b>(4)</b> Certificatul de atestare profesională care conferă
              titularului dreptul de a efectua activitatea pentru care a fost
              eliberat este valabil numai însoţit de permisul de conducere
              corespunzător categoriei din care face parte vehiculul condus.<br /><br />
              <b>Art. 67</b> Se interzice transportul pe drumurile publice al
              mărfurilor şi produselor periculoase în vehicule care nu au
              dotările şi echipamentele necesare sau care nu îndeplinesc
              condiţiile tehnice şi de agreere prevăzute în Acordul european
              referitor la transportul rutier internaţional al mărfurilor
              periculoase (A.D.R.), încheiat la Geneva la 30 septembrie 1957, la
              care România a aderat prin Legea nr. 31/1994, ori pentru care
              conducătorul vehiculului nu deţine certificat A.D.R.
              corespunzător.<br /><br />
              <b>Art. 68</b><br />
              <b>(1)</b> Autovehiculul care transportă mărfuri sau produse
              periculoase poate circula pe drumurile publice numai în condiţiile
              prevăzute de reglementările în vigoare.<br />
              <b>(2)</b> Vehiculele care, prin construcţie sau datorită
              încărcăturii transportate, depăşesc masa şi/sau gabaritul
              prevăzute de normele legale pot circula pe drumul public numai pe
              traseele stabilite de administratorul drumului public sau, după
              caz, de autorităţile administraţiei publice locale, cu respectarea
              prevederilor legale în vigoare.<br /><br />
              <b>Art. 69</b> Autovehiculele cu mase şi/sau gabarite depăşite,
              cele care transportă mărfuri sau produse periculoase, precum şi
              cele de însoţire trebuie să aibă montate semnalele speciale de
              avertizare cu lumină galbenă, prevăzute la art. 32 alin. (1) lit.
              c), iar conducătorii acestora trebuie să le menţină în funcţiune
              pe toată perioada deplasării pe drumul public.<br /><br />
              <b
                >SECŢIUNEA a 3-a<br />
                Reguli pentru alţi participanţi la trafic</b
              ><br /><br />
              <b>Art. 70</b><br />
              <b>(1)</b> Pentru a conduce un moped pe drumurile publice,
              conducătorul acestuia trebuie să aibă vârsta de cel puţin 16
              ani.<br />
              <b>(2)</b> Pentru a conduce o bicicletă sau o trotinetă electrică
              pe drumurile publice, conducătorul acesteia trebuie să aibă vârsta
              de cel puţin 14 ani.<br />
              <b>(3)</b> Bicicletele, mopedele şi trotinetele electrice care
              circulă pe drumurile publice trebuie să fie echipate cu mijloace
              de iluminare şi dispozitive reflectorizantfluorescente. Este
              interzisă circulaţia acestora pe timp de noapte fără aceste
              mijloace şi dispozitive în stare de funcţionare.<br />
              <b>(4)</b> Casca de protecţie este obligatorie pentru conducătorii
              de biciclete şi trotinete electrice a căror vârstă este sub 16
              ani, atunci când se deplasează pe partea carosabilă.<br />
              <b>(5)</b> Se interzice transportul pasagerilor pe trotinete
              electrice.<br /><br />
              <b>Art. 71</b><br />
              <b>(1)</b> Sunt interzise accesul şi deplasarea vehiculelor cu
              tracţiune animală, a maşinilor şi utilajelor autopropulsate
              utilizate în lucrări de construcţii, agricole şi forestiere, a
              animalelor de povară, de tracţiune sau de călărie, precum şi a
              animalelor izolate sau în turmă pe drumurile naţionale, în
              municipii şi pe drumurile la începutul cărora există indicatoare
              de interzicere a accesului.<br />
              <b>(2)</b> Autorităţile administraţiei publice locale au obligaţia
              să amenajeze, cu acordul administratorului drumului public şi
              avizul poliţiei rutiere, drumuri laterale, căi de acces către
              aceste drumuri, precum şi locuri de traversare a drumurilor
              publice destinate circulaţiei animalelor şi vehiculelor prevăzute
              la alin. (1), semnalizate corespunzător.<br />
              <b>(3)</b> Pentru a circula pe drumurile publice pe care accesul
              le este permis, animalele, vehiculele trase sau împinse cu mâna,
              animalele de povară, de tracţiune şi de călărie vor avea câte un
              conducător, iar vehiculele cu tracţiune animală vor fi echipate şi
              cu mijloace de iluminare şi dispozitive
              reflectorizant-fluorescente, în conformitate cu prevederile
              regulamentului.<br /><br />
              <b>Art. 72</b><br />
              <b>(1)</b> Pietonii sunt obligaţi să se deplaseze numai pe
              trotuar, iar în lipsa acestuia, pe acostamentul din partea stângă
              a drumului, în direcţia lor de mers. Când şi acostamentul
              lipseşte, pietonii sunt obligaţi să circule cât mai aproape de
              marginea din partea stângă a părţii carosabile, în direcţia lor de
              mers.<br />
              <b>(2)</b> Pietonii au prioritate de trecere faţă de conducătorii
              de vehicule numai atunci când sunt angajaţi în traversarea
              drumurilor publice prin locuri special amenajate, marcate şi
              semnalizate corespunzător, ori la culoarea verde a semaforului
              destinat pietonilor.<br />
              <b>(3)</b> Traversarea drumului public de către pietoni se face
              perpendicular pe axa acestuia, numai prin locurile special
              amenajate şi semnalizate corespunzător, iar în lipsa acestora, în
              localităţi, pe la colţul străzii, numai după ce s-au asigurat că o
              pot face fără pericol pentru ei şi pentru ceilalţi participanţi la
              trafic.<br />
              <b>(4)</b> Pietonii surprinşi şi accidentaţi ca urmare a
              traversării prin locuri nepermise, la culoarea roşie a semaforului
              destinat acestora, sau a nerespectării altor obligaţii stabilite
              de normele rutiere poartă întreaga răspundere a accidentării lor,
              în condiţiile în care conducătorul vehiculului respectiv a
              respectat prevederile legale privind circulaţia prin acel
              sector.<br />
              <b>(5)</b> Sunt asimilate pietonilor persoanele care conduc un
              scaun rulant de construcţie specială, cele care conduc vehicule
              destinate exclusiv tragerii sau împingerii cu mâna, cele care se
              deplasează pe patine sau dispozitive cu role fără mijloace de
              autopropulsie, precum şi cele care conduc vehicule a căror viteză
              maximă prin construcţie nu depăşeşte 6 km/h şi sunt echipate cu un
              motor electric.<br />
              <b>(6)</b> Se exceptează de la respectarea regulilor stabilite
              pentru pietoni poliţistul rutier şi persoanele care se află pe
              platforma drumului public şi sunt autorizate, în exercitarea
              atribuţiilor de serviciu, să îndrume sau să dirijeze circulaţia
              rutieră, în condiţiile stabilite prin regulament.<br />
              <b>(7)</b> Este interzisă ocuparea trotuarelor cu vehicule
              imobilizate, iar când aceasta este permisă, conform indicatoarelor
              sau marcajelor, lățimea minimă a trotuarului lăsat la dispoziția
              pietonilor trebuie să fie de cel puțin un metru.<br />
              <b>*NOTĂ</b> Începând cu data de 04-10-2023, în conformitate cu
              prevederile punctului 1 al articolului I coroborate cu
              dispozițiile alineatului (1) al articolului II din LEGEA nr. 278
              din 04 octombrie 2022, publicată în MONITORUL OFICIAL nr. 967 din
              4 octombrie 2022, articolul 72 se completează cu alineatul (8) și,
              va avea următorul cuprins:<br />
              <b>(8)</b> Administratorul drumului are obligația să asigure
              iluminarea corespunzătoare a trecerilor nesemaforizate marcate și
              semnalizate corespunzător, cu sisteme inteligente de iluminat tip
              led cu lumină asimetrică pentru crearea unui contrast puternic
              între trecerea de pietoni și suprafața carosabilului, de pe
              sectoarele de drumuri internaționale «E», drumuri naționale și
              drumuri de interes județean situate în intravilanul
              localităților.<br /><br />
              <b>Art. 73</b> Regulile pentru circulaţia pe drumurile publice a
              bicicletelor, a trotinetelor electrice, a vehiculelor cu tracţiune
              animală, a conducătorilor de coloane militare, cortegii, grupuri
              organizate, precum şi de animale se stabilesc prin regulament.<br /><br />
              <b
                >SECŢIUNEA a 4-a<br />
                Circulaţia pe autostrăzi şi drumuri expres</b
              ><br /><br />
              <b>Art. 74</b><br />
              <b>(1)</b> Pe autostrăzi sau pe drumurile expres este interzisă
              circulaţia pietonilor, a autovehiculelor cu gabarite sau mase
              depăşite, fără autorizaţie specială de transport eliberată de
              administratorul drumului public, conform reglementărilor în
              vigoare, a vehiculelor cu tracţiune animală, a animalelor, a
              vehiculelor trase sau împinse cu mâna, a bicicletelor,
              trotinetelor electrice şi mopedelor, a tractoarelor agricole sau
              forestiere şi a maşinilor autopropulsate pentru lucrări agricole,
              precum şi a vehiculelor care, prin construcţie sau din alte cauze,
              nu pot depăşi viteza de 50 km/h.<br />
              <b>(2)</b> De asemenea, pe autostrăzi sau pe drumurile expres sunt
              interzise învăţarea conducerii unui vehicul, încercările
              prototipurilor de şasiuri şi de autovehicule, manifestaţiile,
              defilările, caravanele publicitare, antrenamentele şi competiţiile
              sportive de orice fel, precum şi cortegiile.<br />
              <b>(3)</b> Se interzice circulaţia, oprirea sau staţionarea
              autovehiculelor pe banda de urgenţă sau acostament, cu excepţia
              cazurilor justificate, precum şi a autovehiculelor cu regim de
              circulaţie prioritar.<br />
              <b>(4)</b> Circulaţia autovehiculelor destinate transportului
              public de persoane sau de mărfuri se efectuează, de regulă, numai
              pe banda marginală din partea dreaptă a autostrăzii sau a drumului
              expres, în sensul de mers.<br /><br />
              <b
                >SECŢIUNEA a 5-a<br />
                Obligaţii în caz de accident</b
              ><br /><br />
              <b>Art. 75</b> Accidentul de circulaţie este evenimentul care
              întruneşte cumulativ următoarele condiţii:<br />
              a) s-a produs pe un drum deschis circulaţiei publice ori şi-a avut
              originea într-un asemenea loc;<br />
              b) a avut ca urmare decesul, rănirea uneia sau a mai multor
              persoane ori avarierea a cel puţin unui vehicul sau alte pagube
              materiale;<br />
              *) Înalta Curte de Casaţie şi Justiţie stabileşte că rănirea uneia
              sau mai multor persoane prevăzută de art. 75 lit. b), cuprinsă în
              definiţia accidentului de circulaţie, ca situaţie premisă a
              infracţiunii de părăsire a locului accidentului prevăzută de art.
              338 alin. (1) din Codul penal, nu are în vedere şi autorănirea,
              când singura persoană rănită este însuşi conducătorul singurului
              autovehicul implicat în accident.<br />
              c) în eveniment a fost implicat cel puţin un vehicul în
              mişcare;<br />
              d) abrogat.<br /><br />
              <b>Art. 76</b><br />
              <b>(1)</b> Vehiculele înmatriculate sau înregistrate, cu excepţia
              celor cu tracţiune animală, care circulă pe drumurile publice,
              trebuie să aibă asigurare obligatorie pentru răspundere civilă în
              caz de pagube produse terţilor prin accidente de circulaţie,
              conform legii.<br />
              <b>(2)</b> Poliţia rutieră dispune măsura reţinerii certificatului
              de înmatriculare sau înregistrare şi retragerea plăcuţelor cu
              numărul de înmatriculare sau de înregistrare a vehiculelor
              neasigurate, aflate în trafic, eliberând dovadă fără drept de
              circulaţie.<br />
              <b>(3)</b> Dacă deţinătorul vehiculului nu face dovada asigurării
              acestuia, după 30 de zile de la data aplicării măsurii prevăzute
              la alin. (2), poliţia rutieră dispune radierea din evidenţă a
              vehiculului.<br /><br />
              <b>Art. 77</b><br />
              <b>(1)</b> Conducătorul unui vehicul implicat într-un accident de
              circulaţie în urma căruia a rezultat moartea sau vătămarea
              integrităţii corporale ori a sănătăţii unei persoane este obligat
              să ia măsuri de anunţare imediată a poliţiei, să nu modifice sau
              să şteargă urmele accidentului şi să nu părăsească locul
              faptei.<br />
              <b>(2)</b> Orice persoană care este implicată sau are cunoştinţă
              de producerea unui accident de circulaţie în urma căruia a
              rezultat moartea sau vătămarea integrităţii corporale ori a
              sănătăţii uneia sau a mai multor persoane, precum şi în situaţia
              în care în eveniment este implicat un vehicul care transportă
              mărfuri periculoase este obligată să anunţe de îndată poliţia şi
              să apeleze numărul naţional unic pentru apeluri de urgenţă 112,
              existent în reţelele de telefonie din România.<br />
              <b>(3)</b> Este interzis oricărei persoane să schimbe poziţia
              vehiculului implicat într-un accident de circulaţie în urma căruia
              a rezultat moartea sau vătămarea integrităţii corporale ori a
              sănătăţii uneia sau a mai multor persoane, să modifice starea
              locului sau să şteargă urmele accidentului fără încuviinţarea
              poliţiei care cercetează accidentul.<br /><br />
              <b>Art. 78</b><br />
              <b>(1)</b> Conducătorului de autovehicul, tractor agricol sau
              forestier ori tramvai, instructorului auto atestat care se află în
              procesul de instruire practică a unei persoane pentru obţinerea
              permisului de conducere, precum şi examinatorului autorităţii
              competente în timpul desfăşurării probelor practice ale examenului
              pentru obţinerea permisului de conducere sau pentru oricare dintre
              categoriile acestuia, implicaţi într-un accident de circulaţie, le
              este interzis consumul de alcool sau de substanţe psihoactive după
              producerea evenimentului şi până la testarea concentraţiei
              alcoolului în aerul expirat sau recoltarea probelor biologice.<br />
              <b>(2)</b> În situaţia în care nu sunt respectate dispoziţiile
              alin. (1), se consideră că rezultatele testului sau ale analizei
              probelor biologice recoltate reflectă starea conducătorului, a
              instructorului auto sau a examinatorului respectiv în momentul
              producerii accidentului.<br /><br />
              <b>Art. 79</b><br />
              <b>(1)</b> Conducătorii de vehicule implicaţi într-un accident de
              circulaţie în urma căruia au rezultat numai avarierea vehiculelor
              şi/sau alte pagube materiale sunt obligaţi:<br />
              a) să scoată imediat vehiculele în afara părţii carosabile ori,
              dacă starea vehiculelor nu permite acest lucru, să le deplaseze
              cât mai aproape de bordură sau acostament, semnalizându-le
              prezenţa;<br />
              b) să se prezinte la unitatea de poliţie competentă pe raza căreia
              s-a produs accidentul în termen de cel mult 24 de ore de la
              producerea evenimentului pentru întocmirea documentelor de
              constatare.<br />
              <b>(2)</b> Se exceptează de la obligaţiile prevăzute la alin. (1)
              lit. b):<br />
              a) conducătorii vehiculelor care încheie o constatare amiabilă de
              accident, în condiţiile legii;<br />
              b) conducătorul de vehicul care deţine o asigurare facultativă de
              avarii auto, iar accidentul de circulaţie a avut ca rezultat numai
              avarierea propriului vehicul.<br /><br />
              <b>Art. 80</b><br />
              <b>(1)</b> Proprietarul, deţinătorul mandatat sau conducătorul
              auto al cărui autovehicul, tractor agricol sau forestier, remorcă
              ori tramvai a fost avariat în alte împrejurări decât într-un
              accident de circulaţie este obligat să se prezinte în 24 de ore de
              la constatare la unitatea de poliţie pe raza căreia s-a produs
              evenimentul, pentru întocmirea documentelor de constatare.<br />
              <b>(2)</b> Persoanele prevăzute la alin. (1) sunt exceptate de la
              obligaţia prezentării la unitatea de poliţie dacă există o
              asigurare facultativă în baza căreia pot fi despăgubite pentru
              avarierea vehiculelor respective.<br /><br />
              <b>Art. 80.1</b> În situaţiile prevăzute la art. 79 şi 80,
              repararea vehiculelor se face pe baza documentului de constatare
              eliberat de unitatea de poliţie sau, după caz, de societăţile din
              domeniul asigurărilor.<br /><br />
              <b>Art. 80.2</b> Persoanele fizice sau juridice autorizate să
              execute lucrări de reparaţii la vehicule avariate au obligaţia să
              ţină evidenţa proprie a reparaţiilor executate, potrivit legii, şi
              să comunice de îndată, la cererea poliţiei rutiere, datele
              înregistrate solicitate de aceasta.<br /><br />
              <b>Art. 81</b><br />
              <b>(1)</b> În vederea întocmirii dosarelor pentru despăgubire, la
              solicitarea societăţilor din domeniul asigurărilor, autoritatea
              care are în evidenţă vehiculele înmatriculate sau înregistrate
              comunică acestora datele referitoare la proprietarii sau
              deţinătorii mandataţi ai vehiculelor.<br />
              <b>(2)</b> La solicitarea Comisiei de Supraveghere a Asigurărilor
              sau a societăţilor din domeniul asigurărilor, poliţia rutieră
              comunică acestora copii ale documentelor de constatare a
              evenimentelor.<br />
              <b>(3)</b> La solicitarea poliţiei rutiere, societăţile din
              domeniul asigurărilor comunică în termenul stabilit de aceasta
              copii ale documentelor referitoare la constatările amiabile de
              accidente înregistrate în evidenţa acestora sau alte date expres
              cerute, în condiţiile legii.<br /><br />
              <b
                >SECŢIUNEA a 6-a<br />
                Circulaţia autovehiculelor în traficul internaţional</b
              ><br /><br />
              <b>Art. 82</b><br />
              <b>(1)</b> Autovehiculele, tractoarele agricole sau forestiere şi
              remorcile înmatriculate în România pot trece frontiera de stat pe
              drumurile publice, dacă îndeplinesc condiţiile cerute pentru a fi
              admise în circulaţia internaţională, iar conducătorii lor posedă
