              legii;<br />
              t) deţinătorul vehiculului nu a preschimbat certificatul de
              înmatriculare sau de înregistrare, în conformitate cu prevederile
              legale;<br />
              u) vehiculul nu are montată una dintre plăcuţele cu numărul de
              înmatriculare sau de înregistrare;<br />
              v) vehiculul are lipsă elemente ale caroseriei ori aceasta este
              într-o stare avansată de degradare;<br />
              x) lipsa dotărilor obligatorii pe autovehiculele şi tractoarele
              agricole sau forestiere destinate învăţării conducerii
              autovehiculelor în procesul instruirii persoanelor în vederea
              obţinerii permisului de conducere, prevăzute de reglementările în
              vigoare.<br />
              y) înmatricularea vehiculului este suspendată, în condiţiile
              legii.<br />
