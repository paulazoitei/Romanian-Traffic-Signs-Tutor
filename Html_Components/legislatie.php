              <b>Art. 23</b><br />
              <b>(1)</b> Dreptul de a conduce un autovehicul, tractor agricol
              sau forestier ori tramvai pe drumurile publice îl are numai
              persoana care posedă permis de conducere valabil, corespunzător
              categoriei din care face parte vehiculul respectiv, sau dovada
              înlocuitoare a acestuia cu drept de circulaţie.<br />
              <b>(2)</b> Au dreptul de a conduce autovehicule, tractoare
              agricole sau forestiere ori tramvaie pe drumurile publice, în
              condiţiile stabilite prin regulament, şi persoanele care urmează
              un curs de pregătire practică, în vederea obţinerii permisului de
              conducere, numai în prezenţa şi sub supravegherea directă a unui
              instructor auto atestat în acest sens, precum şi a examinatorului
              din cadrul autorităţii competente în timpul desfăşurării probelor
              practice ale examenului pentru obţinerea permisului de conducere
              pentru oricare dintre categoriile prevăzute de lege.<br />
              <b>(3)</b> Pe parcursul activităţii de pregătire practică sau de
              examinare în vederea obţinerii permisului de conducere,
              instructorul auto atestat ori, după caz, examinatorul împreună cu
              persoana pe care o supraveghează sau o examinează răspund pentru
              încălcarea de către aceasta a regulilor de circulaţie sau, după
              caz, pentru pagubele produse terţilor ca urmare a producerii unui
              accident de circulaţie.<br />
              <b>(3.1)</b> Pregătirea teoretică şi practică a persoanelor în
              vederea obţinerii permisului de conducere este precedată de o
              evaluare obligatorie a capacităţilor psihologice solicitate în
              activitatea de conducere a autovehiculelor, tractoarelor agricole
              sau forestiere ori tramvaielor. Evaluarea psihologică este
              obligatorie în vederea obţinerii certificatului de atestare
              profesională pentru conducătorii de autovehicule şi de tramvaie
              stabiliţi prin prezenta ordonanţă de urgenţă.<br />
              <b>(3.2)</b> Conducătorii de autovehicule sau de tramvaie,
              deţinători ai certificatului de atestare profesională, vor fi
              evaluaţi periodic din punct de vedere al capacităţilor psihologice
              solicitate în activitatea de conducere a autovehiculelor sau
              tramvaielor, în condiţiile stabilite de reglementările în
              vigoare.<br />
              <b>(3.3)</b> Evaluarea psihologică se efectuează în condiţiile
              stabilite prin ordin al ministrului transporturilor,
              construcţiilor şi turismului, în condiţiile legii, care se publică
              în Monitorul Oficial al României, Partea I.<br />
              <b>(3.4)</b> Evaluarea psihologică se realizează în laboratoare de
              specialitate autorizate din cadrul instituţiilor abilitate.<br />
              <b>(3.5)</b> Lista laboratoarelor de specialitate autorizate se
              stabileşte şi se actualizează prin ordin al ministrului
              transporturilor, construcţiilor şi turismului, care se publică în
              Monitorul Oficial al României, Partea I.<br />
              <b>(4)</b> Persoanele care solicită prezentarea la examen pentru
              obţinerea permisului de conducere sau a unor noi categorii ori
              subcategorii ale acestuia trebuie să îndeplinească, potrivit
              legii, condiţiile de vârstă, să fie apte din punct de vedere
              medical şi să facă dovada pregătirii teoretice şi practice prin
              cursuri organizate de unităţi autorizate, potrivit legii.<br />
              <b>(5)</b> Abrogat.<br />
              <b>(6)</b> Pot fi atestaţi ca profesori de legislaţie sau
              instructori auto pentru pregătirea practică, fără şcolarizare şi
              susţinerea examenului de atestare, poliţiştii rutieri cu grad de
              ofiţer cărora le-au încetat raporturile de serviciu şi care şi-au
              desfăşurat activitatea neîntrerupt în ultimii 10 ani în
              structurile poliţiei rutiere.<br />
              <b>(7)</b> Poliţiştii rutieri care nu au grad de ofiţer pot fi
              atestaţi ca instructori auto pentru pregătirea practică, dacă
              îndeplinesc condiţiile prevăzute la alin. (6).<br />
              <b>(8)</b> Abrogat.<br />
              <b>(9)</b> Examenul pentru obţinerea permisului de conducere
              constă în susţinerea unei probe teoretice de verificare a
              cunoştinţelor şi a unei probe practice de verificare a
              aptitudinilor şi comportamentului, corespunzător categoriei de
              permis solicitat. Proba practică pentru categoria AM constă numai
              în verificarea aptitudinilor în poligoane special amenajate.
              Condiţiile de obţinere a permisului de conducere se stabilesc prin
              regulament.<br />
              <b>(9.1)</b> Pentru asigurarea și verificarea legalității
              examenului de obținere a permisului de conducere, modul de
              desfășurare a probei teoretice se înregistrează video, iar în
              cazul probei practice atât video, cât și audio, cu sisteme tehnice
              de monitorizare. Pentru proba practică, sistemele tehnice de
              monitorizare se instalează pe autovehiculele folosite la
              susținerea probei practice, iar în cazul categoriilor A1, A2 și A,
              acestea se instalează pe autovehiculul în care se află
              examinatorul.<br />
              <b>(9.2)</b> Prin excepţie de la prevederile alin. (9.1), nu se
              înregistrează audio sau video proba practică susținută în poligon
              pentru categoriile AM, A1, A2 și A.<br />
              <b>(9.3)</b> Sistemele tehnice de monitorizare prevăzute la alin.
              (1) se asigură de către serviciul public comunitar regim permise
              de conducere şi înmatriculare a vehiculelor care organizează
              examenul de obţinere a permisului de conducere.<br />
              <b>(9.4)</b> Procedura şi condiţiile tehnice în care se realizează
              şi stochează înregistrările prevăzute la alin. (9.1), precum şi
              măsurile pentru protejarea acestora şi a datelor cu caracter
              personal se stabilesc, în condiţiile legii, prin ordin al
              ministrului afacerilor interne.<br />
              <b>(10)</b> Procedura de examinare pentru obținerea permisului de
              conducere se stabilește prin ordin al ministrului afacerilor
              interne, care se publică în Monitorul Oficial al României, Partea
              I.<br /><br />
              <b>Art. 23.1</b><br />
              <b>(1)</b> Persoanele cu domiciliul sau reședința în România ori
              care dovedesc că se află la studii în România de cel puțin 6 luni
              pot solicita obținerea permisului de conducere autorităților
              competente să le examineze.<br />
              <b>(2)</b> În sensul prezentei ordonanțe de urgență, pentru
              procedurile aferente permisului de conducere, au domiciliul sau
              reședința în România persoanele care se află în una dintre
              următoarele situații:<br />
              a) locuiesc în mod obișnuit cel puțin 185 de zile într-un an
              calendaristic la o adresă sau la adrese din România datorită unor
              legături personale și profesionale ori datorită unor legături
              personale care relevă legături strânse cu adresa sau adresele din
              România, dacă persoanele nu au legături profesionale;<br />
              b) revin periodic la o adresă din România datorită unor legături
              personale cu adresa respectivă, deși locuiesc alternativ în locuri
              diferite, situate în două sau mai multe state membre ale Uniunii
              Europene ori ale Spațiului Economic European ori în Confederația
              Elvețiană, deoarece legăturile lor profesionale sunt într-un loc
              diferit de cel al legăturilor personale;<br />
              c) locuiesc în mod obișnuit la o adresă din România datorită
              legăturilor personale cu adresa respectivă, deși locuiesc temporar
              și în alt stat membru al Uniunii Europene ori al Spațiului
              Economic European ori în Confederația Elvețiană pentru
              îndeplinirea unei activități sau misiuni cu durată determinată.<br />
              <b>(3)</b> Persoanele care și-au stabilit domiciliul sau reședința
              în România potrivit Ordonanței de urgență a Guvernului nr. 97/2005
              privind evidența, domiciliul, reședința și actele de identitate
              ale cetățenilor români, republicată, cu modificările și
              completările ulterioare, pot solicita obținerea permisului de
              conducere autorităților competente să le examineze, dacă se află
              în una dintre situațiile prevăzute la alin. (2).<br />
              <b>(4)</b> Participarea la studii în cadrul unei universități sau
              școli din România nu implică transferul domiciliului sau al
              reședinței în România.<br />
              <b>(5)</b> Dovada încadrării în una dintre situațiile prevăzute la
              alin. (2), precum și a studiilor în România se face, în condițiile
              stabilite prin ordinul ministrului afacerilor interne prevăzut la
              art. 23 alin. (10), cu documente:<br />
              a) prevăzute de actele normative care reglementează domiciliul,
              reședința și actele de identitate ale cetățenilor români, libera
              circulație pe teritoriul României a cetățenilor statelor membre
              ale Uniunii Europene, Spațiului Economic European și a cetățenilor
              Confederației Elvețiene sau regimul străinilor în România;<br />
              b) emise de instituția/instituțiile de învățământ,
              acreditată/acreditate în condițiile legii, care să ateste că
              persoana urmează studii în România de cel puțin 6 luni;<br />
              c) alte documente justificative, după caz.<br /><br />
              <b>Art. 24</b><br />
              <b>(1)</b> Examinarea pentru obţinerea permisului de conducere şi
              eliberarea acestuia se realizează de către autoritatea competentă
              pe raza căreia solicitanţii îşi au domiciliul ori reşedinţa în
              sensul art. 23.1 alin. (2) sau, în cazul celor aflaţi la studii în
              România de cel puţin 6 luni, de către autoritatea competentă pe
              raza căreia se află adresa unde locuiesc.<br />
              <b>(1.1)</b> Prin excepţie de la prevederile alin. (1), candidaţii
              care solicită obţinerea categoriilor Tr, Tb sau Tv, precum şi
              candidaţii cu dizabilităţi fizice pot fi examinaţi de autoritatea
              competentă pe raza căreia se află şcoala de conducători auto
              autorizată unde aceştia au absolvit cursurile de pregătire
              teoretică şi practică.<br />
              <b>(2)</b> Forma şi conţinutul permisului de conducere se
              stabilesc prin ordin al ministrului administraţiei şi internelor,
              care se publică în Monitorul Oficial al României, Partea I.<br />
              <b>(3)</b> În localităţile în care autoritatea competentă
              autorizează desfăşurarea cursurilor de pregătire şi susţinerea
              examenului pentru obţinerea permisului de conducere, acestea se
              pot organiza, la cerere, şi în limba minorităţii naţionale
              respective.<br />
              <b>(4)</b> În cazul cetăţenilor străini şi al cetăţenilor statelor
              membre ale Uniunii Europene care îşi stabilesc domiciliul ori
              reşedinţa în România sau, după caz, se află la studii în România,
              examinarea la proba teoretică pentru obţinerea permisului de
              conducere se poate efectua, la cerere, într-o limbă de circulaţie
              internaţională.<br />
              <b>(5)</b> Valabilitatea administrativă a permiselor de conducere,
              pe categorii de vehicule, este următoarea:<br />
              a) 5 ani pentru permisele de conducere eliberate pentru vehiculele
              din categoriile C1, C1E, C, CE, D1, D1E, D, DE, Tr, Tb şi Tv;<br />
              b) 10 ani pentru permisele de conducere eliberate pentru
              vehiculele din categoriile AM, A1, A2, A, B, Bl şi BE.<br />
              <b>(5.1)</b> În cazul pierderii, furtului sau al deteriorării
              permiselor de conducere ori al schimbării numelui titularului,
              autorităţile competente eliberează, după caz, un duplicat a cărui
              valabilitate nu poate depăşi valabilitatea administrativă a
              documentului înlocuit sau alt permis de conducere.<br />
              <b>(5.2)</b> Eliberarea unui nou permis de conducere cu o nouă
              valabilitate administrativă se realizează numai după efectuarea
              examinării medicale prevăzute la art. 22.<br />
              <b>(5.3)</b> În cazurile prevăzute la alin. (5.1) şi (5.2),
              eliberarea permiselor de conducere respective se realizează fără
              susţinerea unui nou examen, la cererea persoanelor care au
              domiciliul sau reşedinţa în România în sensul art. 23.1 alin. (2)
              ori care dovedesc că se află la studii în România de cel puţin 6
              luni şi care deţin permis de conducere eliberat de autorităţile
              competente române, în condiţiile stabilite prin ordin al
              ministrului afacerilor interne, care se publică în Monitorul
              Oficial al României, Partea I.<br />
              <b>(5.4)</b> La cererea titularului permisului de conducere,
              autoritatea competentă eliberează, pentru situațiile prevăzute la
              alin. (5^1) și (5^2), o dovadă înlocuitoare a acestuia, valabilă
              până la emiterea permisului de conducere, dar nu mai mult de 30 de
              zile.<br />
              <b>(6)</b> Persoana care nu deține permis de conducere și a fost
              condamnată, prin hotărâre judecătorească rămasă definitivă, pentru
              oricare dintre infracțiunile prevăzute la art. 334, art. 335 alin.
              (1), art. 336, art. 337, art. 338 alin. (1) și art. 339 alin.
              (2)-(4) din Codul penal sau pentru o infracțiune care a avut ca
              rezultat uciderea sau vătămarea corporală a unei persoane,
              săvârșită ca urmare a nerespectării regulilor de circulație,
              precum și persoana al cărei permis de conducere a fost anulat în
              condițiile art. 114 alin. (1) lit. a)-d) sau ale art. 115 alin.
              (1) au dreptul de a se prezenta la examen pentru obținerea
              permisului de conducere, respectiv pentru obținerea unui nou
              permis de conducere, după caz, dacă a intervenit una dintre
              situațiile următoare:<br />
              a) au trecut 6 luni de la data executării pedepsei amenzii sau a
              pedepsei în regim de privare de libertate;<br />
              b) a trecut un an de la data grațierii totale ori a restului de
              pedeapsă, de la data rămânerii definitive a hotărârii
              judecătorești prin care s-a dispus suspendarea executării pedepsei
              sub supraveghere sau de la data împlinirii termenului de
              prescripție a executării pedepsei;<br />
              c) a intervenit amnistia;<br />
              d) interzicerea dreptului de a conduce anumite categorii de
              vehicule stabilite de instanță, prevăzută la art. 66 alin. (1)
              lit. i) din Codul penal, a fost executată integral ori executarea
              acesteia a încetat în alt mod prevăzut de lege;<br />
              e) a intervenit reabilitarea.<br />
              <b>(6.1)</b> Pentru prezentarea la examen în vederea obținerii
              unui nou permis de conducere, pentru categoriile avute anterior,
              persoana al cărei permis de conducere a fost anulat, aflată în una
              dintre situațiile prevăzute la alin. (6), trebuie să facă dovada
              că este aptă din punct de vedere medical.<br />
              <b>(6.2)</b> Persoana căreia i-a fost anulat permisul de
              conducere, ca urmare a condamnării pentru o infracțiune care a
              fost dezincriminată, în condițiile art. 4 din Codul penal, se
              poate prezenta la autoritatea competentă pe raza căreia își are
              domiciliul sau reședința pentru eliberarea unui nou permis de
              conducere, în condițiile ordinului ministrului afacerilor interne
              prevăzut la alin. (5^3), fără a mai susține examen și cu
              prezentarea documentului care atestă o astfel de situație.<br />
              <b>(6.3)</b> În situația în care anularea permisului de conducere
              nu a fost dispusă până la depunerea cererii de restituire de către
              titularul permisului de conducere, condamnat definitiv pentru o
              infracțiune care a fost dezincriminată, în condițiile art. 4 din
              Codul penal, șeful serviciului rutier competent dispune
              restituirea acestuia, în baza documentului care atestă o astfel de
              situație.<br />
              <b>(7)</b> La eliberarea permisului de conducere, serviciile
              publice comunitare regim permise de conducere și înmatriculare a
              vehiculelor înscriu în Registrul național de evidență a permiselor
              de conducere și a vehiculelor înmatriculate, prevăzut la art. 11
              alin. (4), următoarele date în format electronic:<br />
              a) datele de identificare a titularului permisului de conducere
              românesc, respectiv: numele de familie, prenumele, codul numeric
              personal sau, dacă este cazul, echivalentul acestuia, potrivit
              legislației naționale aplicabile, fotografia și semnătura
              titularului, locul și data nașterii, domiciliul sau reședința,
              precum și, opțional, un număr de telefon și adresa de poștă
              electronică ale persoanei;<br />
              b) categoriile de vehicule pentru care posedă drept de
              conducere;<br />
              c) codurile armonizate ale Uniunii Europene;<br />
              d) datele de identificare a permisului de conducere;<br />
              e) codurile naționale valabile doar pentru conducere pe teritoriul
              României.<br />
              <b>(8)</b> Permisele de conducere românești care au fost
              preschimbate cu documente similare în alte state își pierd
              valabilitatea. Notificările de preschimbare transmise de statele
              emitente se înscriu în Registrul național de evidență a permiselor
              de conducere și a vehiculelor înmatriculate de către Direcția
              Regim Permise de Conducere și Înmatriculare a Vehiculelor sau de
              către serviciile publice comunitare regim permise de conducere și
              înmatriculare a vehiculelor.<br />
              <b>(9)</b> Datele prevăzute la alin. (7) se șterg după 10 ani de
              la data încetării valabilității permisului de conducere.<br /><br />
              <b>Art. 24.1</b><br />
              <b>(1)</b> Permisele de conducere se eliberează după cum
              urmează:<br />
              a) pentru categoriile C1, C, D1 şi D - conducătorilor auto care
              deţin permis de conducere categoria B şi care au promovat examenul
              corespunzător categoriei solicitate;<br />
              b) pentru categoriile BE, C1E, CE, D1E şi DE - conducătorilor auto
              care deţin permis de conducere pentru categoriile B, C1, C, D1,
              respectiv D şi care au promovat examenul corespunzător categoriei
              solicitate.<br />
              <b>(2)</b> Permisele de conducere dau dreptul de a conduce
              vehicule pe drumurile publice, după cum urmează:<br />
              a) permisele de conducere eliberate pentru categoriile C1E, CE,
              D1E sau DE sunt valabile pentru ansamblurile de vehicule din
              categoria BE;<br />
              b) permisele de conducere eliberate pentru categoria CE sunt
              valabile pentru categoria DE, dacă titularii deţin permis de
              conducere pentru categoria D;<br />
              c) permisele de conducere eliberate pentru categoria CE, respectiv
              categoria DE sunt valabile pentru ansamblurile de vehicule din
              categoria C1E, respectiv categoria D1E;<br />
              d) permisele de conducere eliberate pentru orice categorie sunt
              valabile pentru vehiculele din categoria AM;<br />
              e) permisele de conducere eliberate pentru categoria A2 sunt
              valabile şi pentru categoria A1;<br />
              f) permisele de conducere eliberate pentru categoria A sunt
              valabile și pentru categoriile A1 și A2, cele eliberate pentru
              categoria B sunt valabile și pentru categoria B1, cele eliberate
              pentru categoria C sunt valabile și pentru categoria C1, iar cele
              eliberate pentru categoria D sunt valabile și pentru categoria
              D1;<br />
              g) permisele de conducere eliberate pentru categoria B sunt
              valabile pentru triciclurile cu motor având o putere de peste 15
              kW, cu condiţia ca deţinătorii permiselor să aibă vârsta de 21 de
              ani împliniţi.<br />
              h) permisele de conducere eliberate pentru una dintre categoriile
              B, BE, C sau CE sunt valabile și pentru categoria Tr, în anumite
              cazuri specifice;<br />
              i) permisele de conducere eliberate numai pentru categoria Tr sunt
              valabile și pentru ansamblul format dintr-un vehicul din categoria
              Tr și una sau două remorci;<br />
              j) permisele de conducere eliberate pentru categoriile D sau DE
              sunt valabile și pentru vehiculele din categoria Tb.<br />
              <b>(2.1)</b> Cazurile specifice pentru care se consideră
              îndeplinită condiția prevăzută la alin. (2) lit. h) pentru a da
