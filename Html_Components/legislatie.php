              substanţelor psihoactive asupra comportamentului conducătorilor de
              autovehicule, tractoare agricole sau forestiere şi tramvaie;<br />
              d) stabileşte semnul distinctiv ce se aplică pe ambalajul
              medicamentelor contraindicate conducerii vehiculelor;<br />
              e) stabileşte şi certifică conţinutul truselor medicale de prim
              ajutor.<br /><br />
              <b>Art. 126</b> Comisia pentru Supraveghere a Asigurărilor
              împreună cu poliţia rutieră întocmesc programe de prevenţie
              rutieră şi îndrumă, coordonează şi controlează modul de aplicare a
              acestora. Fondurile necesare desfăşurării acestor programe se
              asigură de către Comisia de Supraveghere a Asigurărilor împreună
              cu societăţile de asigurare autorizate.<br /><br />
              <b>Art. 127</b><br />
              <b>(1)</b> În scopul coordonării unei strategii unitare,
              multidisciplinare în domeniul siguranţei circulaţiei rutiere, în
              subordinea Guvernului funcţionează Consiliul Interministerial
              pentru Siguranţa Rutieră.<br />
              <b>(2)</b> Atribuţiile Consiliului Interministerial pentru
              Siguranţa Rutieră se stabilesc prin hotărâre a Guvernului.<br /><br />
              <b>Art. 128</b><br />
              <b>(1)</b> Autorităţile administraţiei publice locale au
              următoarele atribuţii:<br />
              a) iau măsuri pentru menţinerea permanentă în stare tehnică bună a
              drumurilor pe care le administrează, precum şi pentru iluminarea
              corespunzătoare a acestora, conform legii;<br />
              b) iau măsuri pentru instalarea, aplicarea şi întreţinerea
              mijloacelor de semnalizare rutieră şi a echipamentelor destinate
              siguranţei circulaţiei, conform standardelor în vigoare, ţinând
              evidenţa acestora;<br />
              c) întocmesc şi actualizează planurile de organizare a circulaţiei
              pentru localităţile urbane şi iau măsuri pentru realizarea
              lucrărilor ce se impun în vederea asigurării fluenţei şi
              siguranţei traficului, precum şi a reducerii nivelurilor de emisii
              poluante, cu avizul poliţiei rutiere;<br />
              d) stabilesc reglementări referitoare la regimul de acces şi
              circulaţie, staţionare şi parcare pentru diferite categorii de
              vehicule, cu avizul poliţiei rutiere;<br />
              e) iau măsuri pentru amenajarea de trotuare şi drumuri laterale
              pentru circulaţia pietonilor, vehiculelor cu tracţiune animală, a
              tractoarelor, de piste pentru biciclete, precum şi de benzi
              destinate exclusiv transportului public de persoane pe drumurile
              pe care le administrează, cu avizul poliţiei rutiere;<br />
              f) înregistrează şi ţin evidenţa vehiculelor nesupuse
              înmatriculării;<br />
              g) iau măsuri pentru ridicarea şi depozitarea, în spaţii special
              amenajate, a autovehiculelor, remorcilor, caroseriilor sau
              subansamblurilor acestora, devenite improprii din punct de vedere
              tehnic pentru a circula pe drumurile publice, abandonate sau
              părăsite pe domeniul public;<br />
              h) iau măsuri pentru asigurarea spaţiului şi depozitării
              vehiculelor cu tracţiune animală, depistate circulând pe drumurile
              publice pe care le este interzis accesul;<br />
              i) sprijină activităţile organizate de Ministerul Educaţiei şi
              Cercetării şi de Ministerul Administraţiei şi Internelor pentru
              educaţia rutieră a elevilor.<br />
              <b>(2)</b> Proiectele de sistematizare a localităţilor, de
              reglementare a circulaţiei, precum şi a drumurilor publice din
              interiorul şi din afara acestora, elaborate de autorităţile
              publice locale, vor fi avizate de şeful poliţiei rutiere a
              judeţului, municipiului Bucureşti sau, după caz, al poliţiei
              rutiere din Inspectoratul General al Poliţiei Române.<br /><br />
              <b>Art. 129</b><br />
              <b>(1)</b> Ministerul Afacerilor Interne şi autorităţile
              administraţiei publice locale iau măsuri pentru popularizarea
              regulilor de circulaţie în rândul tuturor persoanelor care
              folosesc drumurile publice şi în acest scop vor prevedea în
              planurile anuale fondurile necesare.<br />
              <b>(2)</b> În unităţile de învăţământ preşcolar, primar şi
              gimnazial obligatoriu se desfăşoară cel puţin o oră de educaţie
              rutieră pe săptămână, pe parcursul anului şcolar.<br />
              <b>(3)</b> Mass-media poate sprijini acţiunile Ministerului
              Afacerilor Interne şi ale autorităţilor administraţiei.<br />
              <b>(4)</b> Poliţia rutieră din cadrul Inspectoratului General al
              Poliţiei Române se va implica în realizarea programelor de
              siguranţă rutieră, stabilite de Comisia Economică pentru Europa a
              Organizaţiei Naţiunilor Unite.<br /><br />
              <b>Art. 130</b> Orele de educaţie rutieră din instituţiile de
              învăţământ preşcolar, primar şi gimnazial se desfăşoară potrivit
              programei aprobate prin ordin comun al ministrului educaţiei şi
              cercetării şi al ministrului administraţiei şi internelor, care se
              publică în Monitorul Oficial al României, Partea I.
              <b>Art. 130.1</b><br />
              <b>(1)</b> Ministerul Afacerilor Interne dezvoltă și administrează
              platforme informatice destinate furnizării de servicii electronice
              cu privire la înmatricularea vehiculelor și dreptul de a conduce
              autovehicule pe drumurile publice.<br />
              <b>(2)</b> În platformele informatice prevăzute la alin. (1) se
              înregistrează:<br />
              a) din oficiu, persoanele juridice de drept privat, proprietari de
              vehicule supuse înmatriculării;<br />
              b) la cerere, persoanele fizice.<br /><br />
