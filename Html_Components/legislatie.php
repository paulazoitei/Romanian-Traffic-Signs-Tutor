              <b>Art. 20.</b><br />
              <b>(1)</b> Pentru a conduce pe drumurile publice autovehicule,
              tramvaie ori tractoare agricole sau forestiere, conducătorii
              acestora trebuie să posede permis de conducere corespunzător.<br />
              <b>(2)</b> Permisele de conducere se eliberează pentru următoarele
              categorii de vehicule: AM, A1, A2, A, B1, B, BE, C1, C1E, C, CE,
              D1, D1E, D, DE, Tr, Tb sau Tv.<br />
              <b>(3)</b> Descrierea categoriilor de vehicule prevăzute la alin.
              (2), pentru care se eliberează permise de conducere, este
              prevăzută în anexa nr. 1.<br />
              <b>(4)</b> Vârsta minimă pentru obţinerea permisului de conducere
              este de:<br />
              a) 16 ani împliniţi, pentru categoriile de vehicule AM, A1 şi
              B1;<br />
              b) 18 ani împliniţi, pentru categoriile de vehicule A2, B, BE, C1,
              C1E şi Tr;<br />
              c) 20 de ani împliniţi, dacă persoana are o experienţă de cel
              puţin 2 ani de conducere a motocicletelor din categoria A2, sau 24
              de ani împliniţi, pentru motocicletele din categoria A;<br />
              d) 21 de ani pentru categoriile de vehicule C, CE, D1 şi D1 E,
              precum şi pentru triciclurile cu motor din categoria A;<br />
              e) 24 de ani pentru categoriile de vehicule D, DE, Tb şi Tv.<br />
              <b>(5)</b> Prin excepție de la vârsta minimă prevăzută la alin.
              (4) lit. d), permisul de conducere pentru categoriile de vehicule
              C și CE se eliberează de la vârsta de 18 ani cu înscrierea în
              acesta a codului național pentru atestarea valabilității
              categoriei numai în cazul conducerii vehiculului pe teritoriul
              României în prezența instructorului auto atestat sau a
              examinatorului din cadrul autorității competente în timpul
              desfășurării cursului practic, respectiv a probelor practice
              pentru obținerea certificatului de calificare profesională
              inițială, denumit în continuare CPI, astfel cum acesta este
              reglementat prin art. 1 alin. (3) din Normele privind pregătirea
              și atestarea profesională a anumitor categorii de conducători
              auto, aprobate prin Ordinul ministrului transporturilor nr.
              1.214/2015 pentru aprobarea normelor privind pregătirea și
              atestarea profesională a personalului de specialitate din domeniul
              transporturilor rutiere, cu modificările și completările
              ulterioare, sau cu înscrierea codului european armonizat privind
              deținerea unui CPI.<br />
              <b>(6)</b> Prin excepție de la vârsta minimă prevăzută la alin.
              (4) lit. e), permisul de conducere pentru categoriile de vehicule
              D și DE se eliberează de la vârsta de 21 de ani, cu înscrierea în
              acesta a codului național pentru atestarea valabilității
              categoriei numai în cazul conducerii vehiculului pe teritoriul
              României în prezența instructorului auto atestat sau a
              examinatorului din cadrul autorității competente în timpul
              desfășurării cursului practic, respectiv a probelor practice
              pentru obținerea CPI sau cu înscrierea codului european armonizat
              privind deținerea unui CPI.<br /><br />
              <b>Art. 21.</b><br />
              <b>(1)</b> Conducătorii de autovehicule, de tractoare agricole sau
              forestiere ori de tramvaie trebuie să aibă cunoştinţele şi
              îndemânarea necesare conducerii şi să fie apţi din punct de vedere
              medical şi psihologic.<br />
              <b>(2)</b> Conducătorii de autovehicule, de tractoare agricole sau
              forestiere ori de tramvaie vor fi verificaţi periodic din punct de
              vedere medical şi al capacităţilor psihologice, în condiţiile
              stabilite de reglementările în vigoare.<br /><br />
              <b>Art. 22.</b><br />
              <b>(1)</b> Examinarea medicală se realizează în scopul
              certificării stării de sănătate şi a calităţilor fizice necesare
              unui conducător de autovehicul, tractor agricol sau forestier ori
              tramvai.<br />
              <b>(2)</b> Afecţiunile medicale incompatibile cu calitatea de
              conducător de autovehicule, tractoare agricole sau forestiere ori
              tramvaie se stabilesc de către Ministerul Sănătăţii Publice şi se
              aprobă prin ordin al ministrului, care se publică în Monitorul
              Oficial al României, Partea I.<br />
              <b>(3)</b> Examinarea medicală se realizează în vederea:<br />
              a) aprobării prezentării la examen pentru obţinerea permisului de
              conducere;<br />
              b) obţinerii certificatului de atestare profesională pentru
              conducătorii de autovehicule, de tractoare agricole sau forestiere
              ori de tramvaie stabiliţi prin prezenta ordonanţă de urgenţă;<br />
              c) verificării periodice, conform reglementărilor în vigoare;<br />
              d) efectuarea unei examinări medicale de specialitate la
              trimiterea medicului, în condiţiile alin. (6).<br />
              <b>(4)</b> Examinarea medicală a solicitanţilor prevăzuţi la alin.
              (3) lit. a) şi b), precum şi a conducătorilor de autovehicule,
              tractoare agricole sau forestiere ori tramvaie se realizează în
              unităţi de asistenţă medicală autorizate, existente la nivelul
              fiecărui judeţ, care au obligaţia luării în evidenţă a
              acestora.<br />
              <b>(5)</b> Lista unităţilor de asistenţă medicală autorizate se
              stabileşte şi se actualizează prin ordin al ministrului sănătăţii,
              care se publică în Monitorul Oficial al României, Partea I. Lista
              se publică şi pe pagina de internet a Ministerului Sănătăţii.<br />
              <b>(6)</b> Orice medic, atunci când constată că un conducător de
              autovehicul, de tractor agricol sau forestier ori tramvai, aflat
              în evidenţa sau îngrijirea sa, prezintă afecţiuni medicale
              prevăzute în ordinul ministrului sănătăţii emis conform alin. (2),
              este obligat să emită bilet de trimitere către o unitate de
              asistenţă medicală autorizată prevăzută la alin. (4), cu
              diagnosticele constatate şi cu datele de contact ale unităţii
              medicale în care medicul trimiţător îşi desfăşoară activitatea şi
              să consemneze acest lucru în fişa medicală a conducătorului.
              Biletul de trimitere va fi comunicat conducătorului, de îndată,
              prin orice mijloc de comunicare prevăzut de Legea nr. 134/2010
              privind Codul de procedură civilă, republicată, cu modificările şi
              completările ulterioare. În baza biletului de trimitere, în termen
              de 3 zile lucrătoare de la comunicarea acestuia, persoana care
              posedă permis de conducere este obligată să se prezinte la
              unitatea de asistenţă medicală autorizată pentru a efectua
              examinarea medicală de specialitate.<br />
              <b>(6.1)</b> În cazul în care unitatea de asistenţă medicală
              autorizată, în urma realizării examenului de specialitate necesar,
              stabileşte că persoana care posedă permis de conducere este inaptă
              medical pentru a conduce un autovehicul, tractor agricol sau
              forestier ori tramvai, aceasta este obligată, în termen de o zi de
              la data constatării, să comunice rezultatul examinării către
              poliţia rutieră în a cărei rază teritorială unitatea îşi
              desfăşoară activitatea, către medicul trimiţător, precum şi
              persoanei declarate inaptă din punct de vedere medical pentru a
              conduce un autovehicul, tractor agricol sau forestier ori tramvai.
              În cazul în care persoana care posedă permis de conducere este
              declarată aptă medical, unitatea de asistenţă medicală autorizată
              comunică, în termen de 15 zile de la constatare, rezultatul
              examinării către medicul trimiţător.<br />
              <b>(6.2)</b> În cazul în care, în termen de 30 zile de la data
              comunicării biletului de trimitere în condiţiile alin. (6),
              medicul trimiţător nu este înştiinţat de către unitatea de
              asistenţă medicală autorizată asupra rezultatului examinării,
              acesta este obligat să comunice poliţiei rutiere pe a cărei rază
              teritorială acesta îşi desfăşoară activitatea numele, prenumele şi
              codul numeric personal al persoanei aflate în evidenţa sau
              îngrijirea sa, precum şi informaţia că aceasta a fost trimisă să
              se prezinte pentru o examinare medicală în conformitate cu
              prevederile alin. (6).<br />
              <b>(6.3)</b> La data primirii comunicării prevăzute la alin. (6.1)
              teza I sau, după caz, la alin. (6.2), poliţia rutieră retrage
              permisul de conducere al persoanei respective, făcând menţiune
              despre aceasta în evidenţa permiselor de conducere reţinute şi a
              sancţiunilor aplicate conducătorilor de autovehicule, tractoare
              agricole sau forestiere ori tramvaie. Măsura dispusă i se
              comunică, de îndată, conducătorului prevăzut la alin. (6).<br />
              <b>(6.4)</b> Persoana care posedă permis de conducere, declarată
              inaptă medical pentru a conduce un autovehicul, tractor agricol
              sau forestier ori tramvai, nu mai are dreptul de a conduce de la
              data comunicării acestui fapt de către poliţia rutieră în
              condiţiile alin. (6.3). În termen de maximum 3 zile lucrătoare de
              la această dată, conducătorul este obligat să se prezinte la
              sediul poliţiei rutiere pentru a preda permisul de conducere, în
              vederea retragerii acestuia, până la încetarea cauzelor pentru
              care s-a luat această măsură.<br />
              <b>(7)</b> Verificarea medicală se efectuează în condiţiile
              stabilite prin ordin al ministrului sănătăţii publice, care se
              publică în Monitorul Oficial al României, Partea I.<br />
              <b>(8)</b> Permisul de conducere al persoanei care este declarată
              inaptă psihologic pentru a conduce autovehicule, tractoare
              agricole sau forestiere ori tramvaie se retrage de către poliţia
              rutieră şi poate fi redobândit numai după încetarea motivului
              pentru care s-a luat această măsură.<br />
              <b>(9)</b> Avizul de apt sau inapt medical poate fi contestat doar
              pe baza unei expertize efectuate de instituţiile medico-legale, la
              solicitarea şi pe cheltuiala părţilor interesate.<br /><br />
              <b>Art. 22.1.</b><br />
              <b>(1)</b> Retragerea permisului de conducere se dispune de către
              poliţia rutieră şi în cazul în care titularul acestuia nu a putut
              fi supus testării în vederea stabilirii concentraţiei de alcool
              pur în aerul expirat sau testării preliminare a prezenţei în
              organism a unor substanţe psihoactive, iar în urma efectuării
              examinării medicale s-au constatat elemente clinice sugestive
              consecutive consumului recent de băuturi alcoolice sau de
              substanţe psihoactive, cu ocazia procedurii de recoltare a
              mostrelor biologice în vederea stabilirii alcoolemiei ori a
              consumului de substanţe psihoactive, eliberându-se o dovadă
              înlocuitoare a permisului de conducere fără drept de
              circulaţie.<br />
              <b>(2)</b> Retragerea permisului de conducere se dispune până la
              ora 8.00 a zilei următoare, dar nu mai puţin de 12 ore.<br /><br />
