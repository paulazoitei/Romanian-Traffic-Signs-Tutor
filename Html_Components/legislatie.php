              Capitolul VIII: Căi de atac împotriva procesului-verbal de
              constatare a contravenţiei
            </p>
            <p class="answer">
              <b>Art. 118</b><br />
              <b>(1)</b> Împotriva procesului-verbal de constatare şi
              sancţionare a contravenţiei se poate depune plângere, în termen de
              15 zile de la comunicare, la judecătoria în a cărei rază de
              competenţă a fost constatată fapta.<br />
              <b>(2)</b> Plângerea suspendă executarea amenzii şi a sancţiunii
              contravenţionale complementare dispuse prin procesul-verbal de
              constatare şi sancţionare a contravenţiei de la data înregistrării
              la judecătoria prevăzută la alin. (1).<br />
              <b>(3)</b> Dovada înregistrării plângerii contravenţionale se
              realizează printr-un document emis de către instanţa de judecată
              sau printr-un extras din portalul instanţelor de judecată
              www.portal.just.ro şi se depune sau se transmite prin orice
              mijloace de comunicare la distanţă ce asigură transmiterea
              textului şi confirmarea primirii acestuia, de către contravenient
              sau de către reprezentantul convenţional al acestuia, la unitatea
              de poliţie din care face parte agentul constatator, care
              efectuează menţiunile corespunzătoare în evidenţe. Permisul de
              conducere reţinut se restituie personal titularului sau
              reprezentantului convenţional al acestuia. În situaţia
              transmiterii dovezii înregistrării plângerii contravenţionale prin
              mijloace de comunicare la distanţă, la cererea scrisă a
              titularului sau a reprezentantului convenţional al acestuia,
              unitatea de poliţie din care face parte agentul constatator poate
              restitui permisul de conducere reţinut la adresa de corespondenţă
              indicată în cuprinsul cererii.<br />
              <b>(4)</b> Suspendarea exercitării dreptului de a conduce se reia
              de drept începând cu ora 00.00 a zilei următoare celei în care a
              fost predat permisul de conducere, după rămânerea definitivă a
              hotărârii judecătoreşti prin care instanţa a respins plângerea
              formulată împotriva procesului-verbal de constatare şi sancţionare
              a contravenţiei sau a admis-o în parte, menţinând sancţiunea
              contravenţională complementară aplicată, dar nu mai târziu de 30
              de zile de la data rămânerii definitive a hotărârii
              judecătoreşti.<br />
              <b>(4.1)</b> Termenul de 30 de zile prevăzut la alin. (4) se
              calculează pe zile pline, începând cu ora 00.00 a zilei următoare
              datei rămânerii definitive a hotărârii judecătoreşti. În situaţia
              în care termenul se împlineşte într-o zi nelucrătoare, acesta se
              prelungeşte până în prima zi lucrătoare care urmează.<br />
              <b>(4.2)</b> În situaţia în care, la data de la care se reia
              executarea suspendării dreptului de a conduce, dreptul de a
              conduce este afectat deja de o altă suspendare a exercitării
              acestuia, perioada de suspendare care trebuie executată curge în
              continuarea perioadei de suspendare aflate deja în curs de
              executare.<br />
              <b>(5)</b> Predarea permisului de conducere de către contravenient
              se face personal, prin reprezentant convenţional sau prin servicii
              de curierat, cu aviz de primire, în termenul prevăzut la alin.
              (4), la serviciul/biroul poliţiei rutiere, care îl va înainta
              unităţii de poliţie care îl are în evidenţă.<br />
              <b>(6)</b> Se consideră îndeplinită obligaţia de predare a
              permisului de conducere şi în cazul în care documentul este
              transmis prin servicii de curierat, cu aviz de primire, în
              termenul prevăzut la alin. (4) la serviciul/biroul poliţiei
              rutiere. Data care se ia în considerare este data predării
              permisului către serviciul de curierat. Dovada înlocuitoare se
              eliberează la data prezentării titularului înăuntrul termenului de
              suspendare a dreptului de a conduce la serviciul/biroul poliţiei
              rutiere la care a înaintat permisul de conducere.<br />
              <b>(7)</b> În cazul în care contravenientul nu predă permisul de
              conducere în termenul prevăzut la alin. (4), durata de suspendare
              a exercitării dreptului de a conduce se majorează de drept cu 30
              de zile, fără îndeplinirea vreunei alte formalităţi.<br />
              <b>(8)</b> În sensul prezentului articol, prin serviciu al
              poliţiei rutiere se înţelege serviciul poliţiei rutiere din
              structura unui inspectorat judeţean de poliţie, Brigada Rutieră
              din cadrul Direcţiei Generale a Municipiului Bucureşti sau biroul
              de poliţie autostrăzi din cadrul Brigăzii Autostrăzi şi Misiuni
              Speciale din structura Inspectoratului General al Poliţiei
              Române.<br /><br />
