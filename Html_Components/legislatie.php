              verificării unor lucrări efectuate în partea carosabilă sau
              executării unor lucrări de drumuri, curăţeniei străzilor,
              deszăpezirii sau tractării, transportului şi depanării
              autovehiculelor rămase în pană sau avariate, precum şi tractoarele
              care tractează utilajele agricole şi tehnologice cu dimensiuni de
              gabarit depăşite.<br />
              <b>(3)</b> Autovehiculele prevăzute la alin. (2) lit. a) şi b)
              trebuie să fie echipate şi cu mijloace speciale sonore de
              avertizare.<br />
              <b>(4)</b> Pe autovehiculele aparţinând poliţiei şi pe cele
              prevăzute la alin. (2) lit. c) pot fi instalate şi dispozitive
              luminoase cu mesaje variabile, destinate participanţilor la
              trafic.<br />
              <b>(4.1)</b> Pe autovehiculele Autorităţii Vamale Române, precum
              şi ale Agenţiei Naţionale de Administrare Fiscală, inscripţionate
              vizibil cu denumirea şi sigla instituţiei din care fac parte, pot
              fi instalate dispozitive luminoase cu mesaje variabile, de care se
              poate face uz pentru oprirea în trafic a autovehiculelor în scopul
              exercitării atribuţiilor de control specifice, potrivit legii.<br />
              <b>(5)</b> Mijloacele speciale de avertizare, luminoase sau
              sonore, se certifică sau se omologhează de autoritatea competentă,
              potrivit legii. Condiţiile de utilizare a acestora se stabilesc
              prin regulament.<br /><br />
              <b>Art. 33</b><br />
              <b>(1)</b> Semnalizarea şi amenajările rutiere necesare pe
              drumurile publice se asigură de către administratorul drumului
              respectiv şi se efectuează numai cu avizul poliţiei rutiere.<br />
              <b>(2)</b> Semnalizarea lucrărilor care se execută pe drumurile
              publice este obligatorie. Aceasta este însoțită pe timpul nopții
              de lămpi cu lumină galbenă intermitentă sau în cascadă și se
              efectuează de către executantul lucrărilor, cu avizul poliției
              rutiere, astfel încât să asigure deplasarea în siguranță a tuturor
              participanților la trafic, conform reglementărilor în vigoare.<br />
              <b>(3)</b> Semnalizarea şi amenajările rutiere se definesc şi se
              realizează în condiţiile stabilite prin regulament, în
              conformitate cu reglementările tehnice în vigoare.<br /><br />
              <b>Art. 34</b> Condiţiile de circulaţie pe viaducte şi în tunele,
              precum şi semnalizarea acestora se stabilesc prin regulament, în
              conformitate cu reglementările tehnice în vigoare.
