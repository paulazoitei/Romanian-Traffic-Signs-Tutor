              înlocuitoare a permisului de conducere se eliberează fără drept de
              circulaţie.<br />
              <b>(4)</b> Permisul de conducere al conducătorului de autovehicul,
              tractor agricol sau forestier ori tramvai, implicat într-un
              accident de circulaţie din care a rezultat uciderea sau vătămarea
              corporală a unei persoane, se reţine de către poliţia rutieră dacă
              acesta a încălcat o regulă de circulaţie, eliberându-se dovada
              înlocuitoare fără drept de circulaţie, în situaţia în care regula
