            <p class="question">Capitolul I: Dispozitii generale</p>
            <p class="answer">
              <b> Art. 1.</b><br />
              <b>(1)</b> Circulaţia pe drumurile publice a vehiculelor,
              pietonilor şi a celorlalte categorii de participanţi la trafic,
              drepturile, obligaţiile şi răspunderile care revin persoanelor
              fizice şi juridice, precum şi atribuţiile unor autorităţi ale
              administraţiei publice, instituţii şi organizaţii sunt supuse
              dispoziţiilor prevăzute în prezenta ordonanţă de urgenţă.<br />
              <b>(2)</b> Dispoziţiile prevăzute în prezenta ordonanţă de urgenţă
              au ca scop asigurarea desfăşurării fluente şi în siguranţă a
              circulaţiei pe drumurile publice, precum şi ocrotirea vieţii,
              integrităţii corporale şi a sănătăţii persoanelor participante la
              trafic sau aflate în zona drumului public, protecţia drepturilor
              şi intereselor legitime ale persoanelor respective, a proprietăţii
              publice şi private, cât şi a mediului.<br />
              <b>(3)</b> Autoritatea competentă în domeniul circulaţiei pe
              drumurile publice privind iniţierea şi avizarea unor reglementări,
              precum şi aplicarea şi exercitarea controlului privind respectarea
              normelor din acest domeniu este Ministerul Administraţiei şi
              Internelor, prin Inspectoratul General al Poliţiei Române.<br />
              <b>(4)</b> Reglementările privind circulaţia pe drumurile publice
              se emit, după caz, de către autorităţile publice centrale sau
              locale cu atribuţii în acest domeniu, numai cu avizul
              Inspectoratului General al Poliţiei Române şi cu respectarea
              acordurilor şi convenţiilor internaţionale la care România este
              parte.<br />
              <b>(5)</b> Prevederile prezentei ordonanţe de urgenţă se aplică
              tuturor participanţilor la trafic, precum şi autorităţilor care au
              atribuţii în domeniul circulaţiei şi siguranţei pe drumurile
              publice şi în domeniul protecţiei mediului.<br /><br />
              <b>Art. 2.</b> Îndrumarea, supravegherea şi controlul respectării
              normelor de circulaţie pe drumurile publice se fac de către
              poliţia rutieră din cadrul Inspectoratului General al Poliţiei
              Române, care are obligaţia să ia măsurile legale în cazul în care
              constată încălcări ale acestora.<br /><br />
              <b>Art. 3.</b> Circulaţia pe drumurile publice din zona de
              frontieră şi din alte zone pentru care, potrivit legii, s-au
              stabilit restricţii se face cu respectarea reglementărilor
              instituite pentru acele zone.<br /><br />
              <b>Art. 4.</b> Controlul circulaţiei vehiculelor aparţinând
              instituţiilor din sistemul de apărare, ordine publică şi siguranţă
              naţională se efectuează de către poliţia rutieră, precum şi de
              personalul desemnat de către aceste instituţii.<br /><br />
              <b>Art. 5</b><br />
              <b>(1)</b> Administratorul drumului public, antreprenorul sau
              executantul lucrărilor, conform competenţelor ce îi revin, cu
              avizul poliţiei rutiere, este obligat să instaleze indicatoare ori
              alte dispozitive speciale, să aplice marcaje pe drumurile publice,
              conform standardelor în vigoare, şi să le menţină în stare
              corespunzătoare.<br />
              <b>(2)</b> Administratorul drumului public sau, după caz,
              antreprenorul ori executantul lucrărilor este obligat să
              semnalizeze corespunzător, cât mai repede posibil, orice obstacol
              aflat pe partea carosabilă, care stânjeneşte sau pune în pericol
              siguranţa circulaţiei, şi să ia toate măsurile de înlăturare a
              acestuia.<br />
              <b>(3)</b> Autorităţile publice locale sunt obligate să amenajeze
              staţiile mijloacelor de transport public de persoane, prevăzute cu
              alveole sau refugii, cu avizul poliţiei rutiere.<br />
              <b>(4)</b> Se interzic amplasarea construcţiilor de orice fel şi
              practicarea actelor de comerţ pe trotuar sau pe acostament, în
              parcările amenajate sau pe partea carosabilă, în condiţiile în
              care acestea ar afecta siguranţa circulaţiei pietonilor şi
              vehiculelor.<br />
              <b>(5)</b> În perimetrul staţiilor mijloacelor de transport public
              de persoane pot fi amenajate doar spaţii:<br />
              a) de aşteptare pentru călători;<br />
              b) pentru comercializarea legitimaţiilor de călătorie.<br />
              <b>(6)</b> În cazul producerii unui eveniment rutier ca urmare a
              stării tehnice necorespunzătoare a drumului public, a
              nesemnalizării sau a semnalizării necorespunzătoare a acestuia,
              precum şi a obstacolelor ori lucrărilor care se execută pe acesta,
              administratorul drumului public, antreprenorul sau executantul
              lucrărilor răspunde, după caz, administrativ, contravenţional,
              civil sau penal, în condiţiile legii.<br />
              <b>(7)</b> Orice măsură de restricţie a circulaţiei pe drumurile
              publice se dispune de către administratorul drumului numai cu
              avizul poliţiei rutiere.<br />
              <b>(8)</b> Prin excepţie de la alin. (7), în cazuri temeinic
              justificate, poliţia rutieră poate dispune, fără acordul
              administratorului drumului public, măsuri temporare de restricţie
              a circulaţiei pe drumurile publice.<br />
              <b>(9)</b> În scopul asigurării desfăşurării în condiţii de
              siguranţă a circulaţiei pe drumurile publice, poliţia rutieră
              poate solicita administratorului drumului public executarea sau,
              după caz, desfiinţarea de amenajări rutiere.<br /><br />
              <b>Art. 6</b><br />
              În sensul prezentei ordonanţe de urgenţă, expresiile şi termenii
              de mai jos au următorul înţeles:<br />
              <b>1. acordare a priorităţii</b> - obligaţia oricărui participant
              la trafic de a nu îşi continua deplasarea sau de a nu efectua
              orice altă manevră, dacă prin acestea îi obligă pe ceilalţi
              participanţi la trafic care au prioritate de trecere să îşi
              modifice brusc direcţia sau viteza de deplasare ori să
              oprească;<br />
              <b>2. acostament</b> - fâşia laterală cuprinsă între limita părţii
              carosabile şi marginea platformei drumului;<br />
              <b>3. amenajări rutiere</b> - totalitatea construcţiilor,
              dispozitivelor şi oricăror mijloace tehnice, altele decât cele
              destinate semnalizării, amplasate pe drumul public în scopul
              asigurării securităţii rutiere;<br />
              <b>4. ansamblu de vehicule</b> - vehiculul cu motor cuplat cu unul
              sau două vehicule, care circulă pe drum ca o unitate;<br />
              <b>5. autostradă</b> - drumul naţional de mare capacitate şi
              viteză, semnalizat special, rezervat exclusiv circulaţiei
              autovehiculelor, cu sau fără remorci, care nu serveşte
              proprietăţii riverane, prevăzut cu două căi unidirecţionale,
              separate printr-o zonă mediană sau, în mod excepţional, prin alte
              modalităţi, cu excepţia unor locuri speciale sau cu caracter
              temporar, având cel puţin două benzi de circulaţie pe sens şi
              bandă de urgenţă, intersecţii denivelate şi accese limitate,
              intrarea şi ieşirea fiind permise numai prin locuri special
              amenajate;<br />
              <b>6. autovehicul</b> - orice vehicul echipat cu motor de
              propulsie, utilizat în mod obișnuit pentru transportul persoanelor
              sau mărfurilor pe drum ori pentru tractarea, pe drum, a
              vehiculelor utilizate pentru transportul persoanelor sau
              mărfurilor. Vehiculele care se deplasează pe șine, denumite
              tramvaie, precum și tractoarele agricole sau forestiere nu sunt
              considerate autovehicule. Troleibuzele sunt considerate
              autovehicule;<br />
              <b>6.1. anvelope de iarnă</b> - anvelope omologate conform
              Directivei 92/23/CE a Consiliului din 31 martie 1992 privind
              pneurile autovehiculelor şi ale remorcilor acestora, precum şi
              montarea lor, Regulamentului Comisiei Economice pentru Europa a
              Organizaţiei Naţiunilor Unite (CEE-ONU) nr. 30 sau Regulamentului
              Comisiei Economice pentru Europa a Organizaţiei Naţiunilor Unite
              CEE-ONU nr. 54, după caz.<br />
              <b>7. bandă de circulaţie</b> - subdiviziunea longitudinală a
              părţii carosabile, materializată prin marcaje rutiere sau alte
              mijloace, dacă are o lăţime corespunzătoare pentru circulaţia
              într-un sens a unui şir de vehicule, altele decât vehiculele care
              se deplasează pe două roţi;<br />
              <b>8. bandă de urgenţă</b> - subdiviziunea longitudinală
              suplimentară, situată la extremitatea din partea dreaptă a
              autostrăzii, în sensul de circulaţie, destinată exclusiv
              staţionării autovehiculelor în cazuri justificate, precum şi
              circulaţiei autovehiculelor cu regim prioritar care se deplasează
              la intervenţii sau în misiuni cu caracter de urgenţă;<br />
              <b>9. bandă reversibilă</b> - banda de circulaţie, marcată şi
              semnalizată, situată lângă axa drumului, destinată circulaţiei
              autovehiculelor într-un sens sau în altul, în funcţie de
              intensitatea traficului;<br />
              <b>10. bicicletă</b> - vehiculul prevăzut cu două roţi, propulsat
              prin forţa musculară, cu ajutorul pedalelor ori manivelelor. Sunt
              asimilate bicicletei vehiculele cu pedale cu pedalare asistată
              echipate cu un motor electric auxiliar cu puterea nominală
              continuă mai mică sau egală cu 250 W, a cărui asistare este
              întreruptă atunci când ciclistul încetează să pedaleze sau este
              redusă progresiv pe măsură ce viteza vehiculului creşte, fiind
              întreruptă complet înainte ca viteza vehiculului să atingă 25
              km/h, precum şi bicicletele cu motor, astfel cum sunt prevăzute în
              Regulamentul (UE) nr. 168/2013 al Parlamentului European şi al
              Consiliului din 15 ianuarie 2013 privind omologarea şi
              supravegherea pieţei pentru vehiculele cu două sau trei roţi şi
              pentru cvadricicluri;<br />
              <b>10.1. trotineta electrică</b> - vehiculul cu două sau trei roţi
              şi ghidon, utilizat pentru transportul unei singure persoane, care
              nu este prevăzut cu loc şezând sau care este echipat cu un loc
              şezând pentru conducător, al cărui punct de şedere de referinţă se
              află la o înălţime mai mică sau egală cu 540 mm în cazul celor cu
              două roţi, respectiv la o înălţime mai mică sau egală cu 400 mm în
              cazul celor cu trei roţi, a cărui viteză maximă prin construcţie
              este mai mare de 6 km/h, dar nu depăşeşte 25 km/h şi care este
              echipat cu motor electric; punctul de şedere de referinţă are
              înţelesul prevăzut în Regulamentul (UE) nr. 168/2013 al
              Parlamentului European şi al Consiliului din 15 ianuarie 2013;<br />
              <b>11. coloană oficială</b> - unul sau mai multe vehicule care
              circulă pe drumul public şi sunt însoţite de unul sau mai multe
              autovehicule ale poliţiei rutiere, care au în funcţiune semnale
              speciale de avertizare sonore şi luminoase de culoare roşie;<br />
              <b>12. conducător</b> - persoana care conduce pe drum un grup de
              persoane, un vehicul sau animale de tracţiune, animale izolate sau
              în turmă, de povară ori de călărie;<br />
              <b>13.</b> abrogat;<br />
              <b>13.1.</b> abrogat;<br />
              <b>14. drum public</b> - orice cale de comunicaţie terestră, cu
              excepţia căilor ferate, special amenajată pentru traficul pietonal
              sau rutier, deschisă circulaţiei publice; drumurile care sunt
              închise circulaţiei publice sunt semnalizate la intrare cu
              inscripţii vizibile;<br />
              <b>14.1. drum pentru ciclişti</b> - sunt căi de comunicaţie
              terestră proiectate şi construite special pentru circulaţia
              cicliştilor;<br />
              <b>15. unităţi de asistenţă medicală autorizate</b> – unităţile
              sanitare cu personal calificat şi cu dotare corespunzătoare pentru
              examinarea medicală ambulatorie a candidaţilor la obţinerea
              permisului de conducere şi a conducătorilor de autovehicule, de
              tractoare agricole sau forestiere ori de tramvaie, autorizate de
              autoritatea de sănătate publică judeţeană sau de cea a
              municipiului Bucureşti, după caz;<br />
              <b>16. intersecţie</b> - orice încrucişare, joncţiune sau
              bifurcare de drumuri la nivel, inclusiv spaţiile formate de
              acestea;<br />
              <b>16.1. înmatriculare/înregistrare</b> - operaţiunea
              administrativă prin care se atestă că un vehicul poate circula pe
              drumurile publice. Dovada atestării înmatriculării/înregistrării
              este certificatul de înmatriculare/înregistrare şi numărul de
              înmatriculare/înregistrare atribuit;<br />
              <b>16.2. înmatriculare/înregistrare permanentă</b> - operaţiunea
              de înmatriculare/înregistrare prin care se atribuie certificat de
              înmatriculare/înregistrare şi număr de înmatriculare/înregistrare
              pentru o perioadă nedeterminată;<br />
              <b>16.3. înmatriculare temporară</b> - operaţiunea de
              înmatriculare prin care se acordă certificat de înmatriculare şi
              număr de înmatriculare pentru o perioadă determinată;<br />
              <b>17. localitate</b> - cătunul, satul, comuna, oraşul şi
              municipiul ale căror intrări şi ieşiri sunt semnalizate în
              conformitate cu prevederile legale;<br />
              <b>18. masă totală maximă autorizată</b> - masa maximă a unui
              vehicul încărcat, declarată admisibilă în urma omologării de către
              autoritatea competentă;<br />
              <b>19. mijloc tehnic certificat</b> - dispozitivul care dovedeşte
              consumul de produse ori substanţe stupefiante sau al
              medicamentelor cu efecte similare acestora ori prezenţa în aerul
              expirat a alcoolului sau prin care se probează încălcări ale unor
              reguli de circulaţie;<br />
              <b>20.mijloc tehnic omologat şi verificat metrologic</b> -
              dispozitivul care stabileşte concentraţia de alcool în aerul
              expirat ori destinat măsurării vitezei;<br />
              <b>21. moped</b> - autovehicul care face parte din categoria
              mopedelor potrivit Regulamentului (UE) nr. 168/2013 al
              Parlamentului European şi al Consiliului din 15 ianuarie 2013.
              Este asimilat mopedului cvadriciclul uşor, astfel cum este
              prevăzut în Regulamentul (UE) nr. 168/2013 al Parlamentului
              European şi al Consiliului din 15 ianuarie 2013;<br />
              <b>22. motocicletă</b> - autovehiculul cu două roţi, cu sau fără
              ataş, echipat cu un motor care are o capacitate cilindrică mai
              mare de 50 cm3 şi/sau a cărui viteză maximă, prin construcţie,
              depăşeşte 45 km/h;<br />
              <b>23. parte carosabilă</b> - porţiunea din platforma drumului
              destinată circulaţiei vehiculelor; un drum poate cuprinde mai
              multe părţi carosabile complet separate una de cealaltă printr-o
              zonă despărţitoare sau prin diferenţă de nivel;<br />
              <b>24. participant la trafic</b> - persoana fizică ce utilizează,
              la un moment dat, drumul public;<br />
              <b>25. pistă pentru biciclete</b> - subdiviziunea părţii
              carosabile, a trotuarului ori a acostamentului, special amenajată,
              semnalizată şi marcată corespunzător;<br />
              <b>26. prioritate de trecere</b> - dreptul unui participant la
              trafic de a trece înaintea celorlalţi participanţi la trafic cu
              care se intersectează, în conformitate cu prevederile legale
              privind circulaţia pe drumurile publice;<br />
              <b>27. remorcă</b> - vehiculul fără motor destinat a fi tractat de
              un autovehicul sau de un tractor agricol sau forestier;<br />
              <b>28. remorcă uşoară</b> - remorca a cărei masă totală maximă
              autorizată nu depăşeşte 750 kg;<br />
              <b>29. semiremorcă</b> - remorca a cărei masă totală maximă
              autorizată este preluată în parte de către un autovehicul sau de
              către un tractor agricol sau forestier;<br />
              <b>29.1. suspendarea înmatriculării</b> - operaţiune
              administrativă care constă în interzicerea temporară a dreptului
              de a pune în circulaţie şi a utiliza un vehicul pe drumul
              public;<br />
              <b>30. tractor agricol sau forestier</b> - orice vehicul cu motor,
              care circulă pe roți sau pe șenile, având cel puțin două axe, a
              cărui principală funcție constă în puterea sa de tracțiune,
              conceput în special pentru a trage, a împinge, a transporta ori a
              acționa anumite echipamente, utilaje sau remorci utilizate în
              exploatarea agricolă ori forestieră și a cărui utilizare pentru
              transportul pe drum al persoanelor sau al mărfurilor ori pentru
              tractarea, pe drum, a vehiculelor utilizate pentru transportul
              persoanelor sau al mărfurilor nu este decât o funcție secundară.
              Sunt asimilate tractorului agricol sau forestier vehiculele
              destinate efectuării de servicii ori lucrări, denumite mașini
              autopropulsate;<br />
              <b>31.</b> abrogat;<br />
              <b>32. trecere la nivel</b> - încrucişarea la nivel dintre un drum
              public şi o cale ferată sau linie de tramvai, care dispune de o
              platformă independentă;<br />
              <b>32.1. triciclu cu motor</b>- autovehicul cu trei roţi dispuse
              simetric, a cărui viteză maximă prin construcţie depăşeşte 45
              km/h, care este echipat cu un motor având o cilindree de peste 50
              cm3, în cazul unui motor cu ardere internă, sau cu un motor
              electric cu putere nominală continuă ce depăşeşte 4 kW;<br />
              <b>33. trotuar</b> - spaţiul longitudinal situat în partea
              laterală a drumului, separat în mod vizibil de partea carosabilă
              prin diferenţă sau fără diferenţă de nivel, destinat circulaţiei
              pietonilor;<br />
              <b>34. deţinător mandatat</b> - persoana fizică sau juridică care
              foloseşte un vehicul în baza unui contract de leasing;<br />
              <b>35. vehicul</b> - sistemul mecanic care se deplasează pe drum,
              cu sau fără mijloace de autopropulsare, utilizat în mod curent
              pentru transportul de persoane şi/sau bunuri ori pentru efectuarea
              de servicii sau lucrări;<br />
              <b>35.1. vehicul nou</b> - vehicul care nu a mai fost niciodată
              înmatriculat;<br />
              <b>35.2. vehicul lent</b> - vehicul cu viteza maximă constructivă
              de până la 25 km/h inclusiv, cu excepţia vehiculelor cu două sau
              trei roţi, a cvadriciclurilor, a tractoarelor agricole sau
              forestiere, a remorcilor destinate a fi tractate de tractoarele
              agricole sau forestiere, precum şi a maşinilor autopropulsate.<br />
              <b>36. vehicul cu mase sau dimensiuni de gabarit depăşite</b> -
              vehiculul care, datorită dimensiunilor sale ori mărfurilor
              transportate, depăşeşte masele totale maxime admise sau
              dimensiunile de gabarit admise, prevăzute în reglementările
              legale;<br />
              <b
                >37. vehicul în circulaţie internaţională sau în trafic
                internaţional</b
              >
              - vehiculul care, prin deplasarea sa, depăşeşte cel puţin o
              frontieră de stat;<br />
              <b>38. zona drumului public</b> cuprinde suprafaţa de teren
              ocupată de elementele constructive ale drumului, zona de protecţie
              şi zona de siguranţă. Limitele zonelor drumurilor se stabilesc în
              conformitate cu prevederile legale;<br />
              <b>39. zonă pietonală</b> - perimetrul care cuprinde una sau mai
              multe străzi rezervate circulaţiei pietonilor, unde accesul
              vehiculelor este supus unor reguli speciale de circulaţie, având
              intrările şi ieşirile semnalizate în conformitate cu prevederile
              legale;<br />
              <b>40. zonă rezidenţială</b> - perimetrul dintr-o localitate unde
              se aplică reguli speciale de circulaţie, având intrările şi
              ieşirile semnalizate în conformitate cu prevederile legale;<br />
              <b>41. conducere auto defensivă</b> - pregătire specială a
              conducătorilor auto în vederea prevenirii accidentelor rutiere.
